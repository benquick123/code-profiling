def unikati(s):
    novi = []
    for a in s:
        if a not in novi:
            novi.append(a)
    return novi


def avtor(tvit):
    return tvit[:tvit.find(":")]


def vsi_avtorji(tviti):
    avtorji = []
    for tvit in tviti:
        avtorji.append(avtor(tvit))
    return unikati(avtorji)


def izloci_besedo(beseda):
    beseda_nova = beseda 
    
    for znak in beseda:
        if not znak.isalnum():
            beseda_nova = beseda_nova[1 : ]
        elif znak.isalnum():
            break
        
    beseda = beseda_nova
    for znak in beseda[::-1]:
        if not znak.isalnum():
            beseda_nova = beseda_nova[: -1]
        elif znak.isalnum():
            break

    return beseda_nova


def se_zacne_z(tvit, c):
    tabelca = tvit.split(" ")
    hashtagi = []

    for beseda in tabelca:
        if beseda[0] == c:
            hashtagi.append(izloci_besedo(beseda))
    return hashtagi

def zberi_se_zacne_z(tviti, c):
    seznam = []
    for tvit in tviti:
        seznam += se_zacne_z(tvit, c)
    return unikati(seznam)

def vse_afne(tviti):
    seznam = zberi_se_zacne_z(tviti, "@")
    return unikati(seznam)

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")
    
def vse_osebe(tviti):
    osebe = vsi_avtorji(tviti)
    osebe += vse_afne(tviti)
    osebe = unikati(osebe)
    osebe.sort()
    return osebe




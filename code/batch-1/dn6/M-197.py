
import unittest
import collections
def besedilo(tvit):
    for i in tvit:
        tvit=tvit.replace(i,"",1)
        if i==":":
            break
    return tvit.strip(" ")

def zadnji_tvit(tviti):
    s1={}
    for i in tviti:
        sez=i.split(":")
        s1[sez[0]]=besedilo(i)
    return s1

def prvi_tvit(tviti):
    s1={}
    for i in tviti:
        sez=i.split(":")
        if sez[0] in s1:
            continue
        s1[sez[0]]=besedilo(i)
    return s1

def prestej_tvite(tviti):
    s1=collections.defaultdict(int)
    for i in tviti:
        sez=i.split(":")
        ime=sez[0]
        s1[ime]=s1[ime]+1
    return dict(s1)

def omembe(tviti):
    s1 = collections.defaultdict(list)
    for i in tviti:
        sez = i.split(":")
        ime = sez[0]
        str = i.split(" ")
        s1[ime]
        for j in str:
            if j[0] == "@":
                if  not j[-1].isalnum():
                    s1[ime].append(j[1:-1])
                else:
                    s1[ime].append(j[1:])
    return dict(s1)

def neomembe(ime,omembe):
    sez=[]
    sez1=[]
    sez2=omembe[ime]
    sez2.append(ime)
    for i in omembe:
        sez.append(i)
    for i in sez:
        if not i in sez2:
            sez1.append(i)
    return sez1

def se_poznata(ime1,ime2,omembe):
    if ime1 in omembe and ime2 in omembe:
        l1 = omembe[ime1]
        l2 = omembe[ime2]
        if ime2 in l1 or ime1 in l2:
            return True
        else:
            return False
    else:
        return False

def hashtagi(tviti):
    s1 = collections.defaultdict(list)
    for i in tviti:
        str = i.split(" ")
        for j in str:
            if j[0] == "#":
                if not j[-1].isalnum():
                    s1[j[1:-1]].append(str[0][:-1])
                else:
                    s1[j[1:]].append(str[0][:-1])
    for j in s1:
        s1[j].sort()
    return dict(s1)



def besedilo(tvit):
    mesto = tvit.find(":")
    tekst = tvit[mesto+2:]
    return tekst

def zadnji_tvit(tviti):
    s =collections.defaultdict(list)
    for tvit in tviti:
        ime = avtor(tvit)
        vsebina = besedilo(tvit)
        s[ime] = vsebina
    return (s)

def prvi_tvit(tviti):
    s =collections.defaultdict(list)
    for tvit in tviti:
        ime = avtor(tvit)
        vsebina = besedilo(tvit)
        if ime not in s:
            s[ime] = vsebina
    return s

def prestej_tvite(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        ime = avtor(tvit)
        if ime not in s:
            s[ime] = 1
        else:
            s[ime] +=1
    return s

def omembe(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        ime = avtor(tvit)
        imena = se_zacne_z(tvit, "@")
        for omenjen in imena:
            s[ime].append(omenjen)
        if imena == []:
            s[ime] =[]
    return s

def neomembe(ime, omembe):
    s = []
    b = set(omembe[ime])
    for neomenjen in omembe:
        if ime not in neomenjen:
            if neomenjen not in b:
                s.append(neomenjen)
    return s



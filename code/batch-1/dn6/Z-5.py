import collections

def unikati(s):
    t = []
    for i in s:
        if i not in t:
            t.append(i)
    return t

def avtor(tvit):
    ime = ""
    i=0
    while tvit[i] != ":":
        ime += tvit[i]
        i+=1
    return ime


def se_zacne_z(tvit, c):
    besede_a =[]
    tvit = tvit.split()
    for a in tvit:
        for b in a:
            if b ==c:
                beseda =izloci_besedo(a)
                besede_a.append(beseda)
    return  besede_a

def izloci_besedo(beseda):
    for beseda_n in beseda:
        if beseda_n.isalnum():
            break
        else:
            beseda = beseda.lstrip(beseda_n[0])
    for beseda_n in beseda[::-1]:
        if beseda_n.isalnum():
            break
        else:
            beseda = beseda.rstrip(beseda_n[-1])
    return beseda


def besedilo(tvit):
    avto = avtor(tvit)
    a = tvit.split(avto)[1]
    return a.lstrip(": ")

def zadnji_tvit(tviti):
    slovar = {}
    for a in tviti:
        c = avtor(a)
        d = besedilo(a)
        slovar[c] = d
    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for x in tviti:
        if avtor(x) not in slovar:
            c = avtor(x)
            d = besedilo(x)
            slovar[c] = d
    return slovar

def prestej_tvite(tviti):
    slovar = {}
    for x in tviti:
        a = avtor(x)
        if a not in slovar:
            slovar[a] = 0
        slovar[a] += 1
    return slovar

def omembe(tviti):
    slovar = {}
    for x in tviti:
        kljuc = avtor(x)
        a = se_zacne_z(x, "@")
        if kljuc in slovar:
            slovar[kljuc].extend(a)
        else:
            slovar[kljuc] = a
    return slovar

def neomembe(ime, omembe):
    s = []
    a = omembe.get(ime)
    for x in omembe.keys():
        if x == ime:
            continue
        if x not in a:
            s.append(x)
    return s


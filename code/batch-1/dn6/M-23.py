def hashtagi(tviti):

    slovar = defaultdict(list)
    for tvit in tviti:
        ime = tvit.split(":", 1)[0]
        a = se_zacne_z(tvit, "#")

        for b in a:

            slovar[b].append(ime)

    for key, val in slovar.items():

       slovar[key] = sorted(val)

    return slovar



import unittest
from collections import defaultdict

#naloga 1
def besedilo(tvit):
    return tvit.split(": ",1)[1:][0]

#naloga 2
def zadnji_tvit(tviti):

    slovarTvitov = {}

    for tvit in tviti:
        ime = tvit.split(":", 1)[0]
        besedilo = tvit.split(": ", 1)[1:][0]

        slovarTvitov[ime] = besedilo

    return slovarTvitov

#naloga 3
def prvi_tvit(tviti):

    slovarTvitov = {}

    for tvit in tviti:
        ime = tvit.split(":", 1)[0]
        besedilo = tvit.split(": ", 1)[1:][0]

        if not ime in slovarTvitov:
            slovarTvitov[ime] = besedilo

    return slovarTvitov

#naloga 4
def prestej_tvite(tviti):

    slovarTvitov = defaultdict(int)

    for tvit in tviti:
        ime = tvit.split(":", 1)[0]

        slovarTvitov[ime] += 1

    return slovarTvitov

#naloga 5
def izloci_besedo(beseda):
    while beseda and not beseda[0].isalnum():
        beseda = beseda[1:]
    while beseda and not beseda[-1].isalnum():
        beseda = beseda[:-1]
    return beseda


def se_zacne_z(tvit, c):
    return [izloci_besedo(beseda) for beseda in tvit.split() if beseda[0] == c]

def omembe(tviti):

    slovarTvitov = defaultdict(list)

    for tvit in tviti:
        ime = tvit.split(":", 1)[0]

        slovarTvitov[ime]

        for val in se_zacne_z(tvit, "@"):
            slovarTvitov[ime].append(val)

    return slovarTvitov

#naloga 6
def neomembe(ime, omembe):
    vsiAvtorji = []
    for pisec, i in omembe.items():

        vsiAvtorji.append(pisec)
        if pisec == ime:
            omenjeniAvtorji = i

    neomenjeniAvtorji = []
    for imena in vsiAvtorji:
        if not imena in omenjeniAvtorji and not imena == ime:
            neomenjeniAvtorji.append(imena)

    return neomenjeniAvtorji

#dodatna naloga 1
def se_poznata(ime1, ime2, omembe):

    for ime, value in omembe.items():

        if ime == ime1:
            for i in value:
                if i == ime2:
                    return True
        elif ime == ime2:
            for i in value:
                if i == ime1:
                    return True
    return False


import unittest

def besedilo(tvit):
    return tvit.split(": ", 1)[1]

def avtor(tvit):
    return tvit.split(":", 1)[0]

def zadnji_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        napisal         = avtor(tvit)
        napisano        = besedilo(tvit)
        slovar[napisal] = napisano

    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        napisal  = avtor(tvit)
        napisano = besedilo(tvit)
        if napisal not in slovar:
            slovar[napisal] = napisano

    return slovar

def prestej_tvite(tviti):
    slovar = {}
    for tvit in tviti:
        napisal = avtor(tvit)
        if napisal not in slovar:
            slovar[napisal] = 1
        else:
            slovar[napisal] += 1

    return slovar

def izloci_besedo(beseda):
    nova_beseda = ''
    for i in range(len(beseda)):
        if i == 0 or i == len(beseda)-1:
            if beseda[i].isalnum():
                nova_beseda += beseda[i]
        else:
            if beseda[i].isalnum() or (beseda[i-1].isalnum() and beseda[i+1].isalnum()):
                nova_beseda += beseda[i]

    return nova_beseda

def se_zacne_z(tvit, c):
    beseda       = ''
    seznam_besed = []
    zapisuj      = False

    for i in range(len(tvit)):
        if tvit[i] == c:
            zapisuj = True
        if (tvit[i].isspace() or i == len(tvit)-1) and zapisuj:
            beseda += tvit[i]
            seznam_besed.append(beseda)
            beseda  = ''
            zapisuj = False
        if zapisuj:
            beseda += tvit[i]

    for i in range(len(seznam_besed)):
        seznam_besed[i] = izloci_besedo(seznam_besed[i])

    return seznam_besed

def omembe(tviti):
    slovar = {}
    for tvit in tviti:
        napisal = avtor(tvit)
        if napisal not in slovar:
            slovar[napisal] = se_zacne_z(tvit, "@")
        else:
            slovar[napisal] += se_zacne_z(tvit, "@")

    return slovar

def neomembe(ime, omembe):
    omenjeni    = omembe[ime]
    neomenjenji = []
    for napisal in omembe:
        if napisal not in omenjeni and napisal != ime:
            neomenjenji.append(napisal)

    return neomenjenji

def se_poznata(ime1, ime2, omembe):
    poznanstvo = relacija(omembe, ime1, ime2)

    if not poznanstvo:
        return relacija(omembe, ime2, ime1)

    return poznanstvo

def relacija(omembe, oseba1, oseba2):
    if oseba1 in omembe.get(oseba2, []):
        return True
    else:
        return False

def hashtagi(tviti):
    slovar = {}
    for tvit in tviti:
        avtorr    = avtor(tvit)
        hashtagii = se_zacne_z(tvit, "#")
        for hashtag in hashtagii:
            if hashtag in slovar:
                slovar[hashtag].append(avtorr)
                slovar[hashtag].sort()
            else:
                slovar[hashtag] = [avtorr]

    return slovar


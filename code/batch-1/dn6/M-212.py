import collections
#prejšnje funkcije

def avtor(tvit):
    tabela = list(tvit)
    rezultat = ""
    for i in range(len(tabela)):
        if tabela[i] == ":":
            break
        rezultat += tabela[i]
    return rezultat

def izloci_besedo(beseda):
    while beseda and not beseda[0].isalnum():
        beseda = beseda[1:]
    while beseda and not beseda[-1].isalnum():
        beseda = beseda[:-1]
    return beseda

#nove funkcije
def besedilo(tvit):
    x=tvit.split(":")[1:]
    return ":".join(x).strip()

def zadnji_tvit(tviti):
    x= collections.defaultdict()
    for tvit in tviti:
        x[avtor(tvit)]= besedilo(tvit)
    return x

def prvi_tvit(tviti):
    x= collections.defaultdict()
    y=[]
    for tvit in tviti:
        if avtor(tvit) not in y:
            x[avtor(tvit)]= besedilo(tvit)
            y.append(avtor(tvit))
    return x

def prestej_tvite(tviti):
    x= collections.defaultdict(int)
    for tvit in tviti:
        x[avtor(tvit)]+=1
    return x

def omembe(tviti):
    x= collections.defaultdict(list)
    for tvit in tviti:
        y= tvit.split()
        o= avtor(tvit)
        if o not in x:
            x[o] = []
        for z in y:
            if z[0] == '@':
                i = izloci_besedo(z)
                x[o].append(i)
    return x

def neomembe(ime, omembe):
    neom = []
    om = []
    for name in omembe:
        om.append(name)
    for oseba in om:
        if oseba not in omembe[ime] and oseba != ime:
            neom.append(oseba)
    return neom





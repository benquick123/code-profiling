def izloci_besedo(beseda):
    n = 0

    for crka in beseda:
        if crka.isalnum() == False:
            n = n + 1
        if crka.isalnum() == True:
            break

    beseda1 = beseda[n:]
    beseda2 = beseda1[::-1]
    n = 0

    for crka in beseda2:
        if crka.isalnum() == False:
            n = n + 1
        if crka.isalnum() == True:
            break
    beseda1 = beseda2[n:]

    return(beseda1[::-1])


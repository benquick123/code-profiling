def besedilo(tvit):
    b = tvit.find(':')
    return tvit[b+2:]

#kljuc = avtor, vrednost = tvit
def zadnji_tvit(tviti):
    s = {}
    for tvit in tviti:
        a = avtor(tvit)
        s[a] = besedilo(tvit)
    return s

#Obdrži prvi tvit osebe
def prvi_tvit(tviti):
    s = {}
    for tvit in tviti:
        a = avtor(tvit)
        if a not in s:
            s[a] = besedilo(tvit)
    return s


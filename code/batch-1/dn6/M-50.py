import collections

def prestej_tvite(tviti):
    s = collections.defaultdict(int)
    for tvit in tviti:
        a = avtor(tvit)
        s[a] += 1
    return s

def omembe(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        a = avtor(tvit)
        c = vse_afne([tvit])
        s[a] += c
    return s


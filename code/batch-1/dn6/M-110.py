def besedilo(tvit):
    beseda=''
    pogoj= False
    for crka in tvit:
        if pogoj==True:
            beseda+=crka
        if crka==':' and pogoj==False:
            pogoj=True

    return beseda

def zadnji_tvit(tviti):
    slovar={}
    for tvit in tviti:
        slovar[avtor(tvit)]=besedilo(tvit)
    return slovar


def prvi_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        if not avtor(tvit) in slovar:
            slovar[avtor(tvit)] = besedilo(tvit)
    return slovar

def prestej_tvite(tviti):
    slovar = {}
    for tvit in tviti:
        if avtor(tvit) in slovar:
            slovar[avtor(tvit)] +=1
        else:
            slovar[avtor(tvit)]= 1
    return slovar

def omembe(tviti):
    slovar = {}
    for tvit in tviti:
        slovar[avtor(tvit)] = se_zacne_z(tvit,'@')
    return slovar


def neomembe(ime, omembe):
    om=omembe[ime]
    om.append(ime)
    nom=[]
    for o in omembe:
        if not o in om:
            nom.append(o)
    return nom



def sosedov(x, y, mine):
    """
    Vrni število sosedov polja s koordinatami `(x, y)` na katerih je mina.
    Polje samo ne šteje.

    Args:
        x (int): koordinata x
        y (int): koordinata y
        mine (set of tuple of int): koordinate min

    Returns:
        int: število sosedov
    """

    stevec = 0

    for x1, y1 in mine:

        if x1 == x and y1 == y:
            continue

        else:
            x1_d = x1 + 1
            x1_l = x1 - 1
            y1_d = y1 + 1
            y1_g = y1 - 1

            if (x1_d == x and y1_d == y) or (x1_d == x and y1_g == y) or (x1_l == x and y1_d == y) or (x1_l == x and y1_g == y) or \
                    (x1_d == x and y1 == y) or (x1_l == x and y1 == y) or (x1 == x and y1_d == y) or (x1 == x and y1_g == y):
                stevec += 1

    return stevec


def najvec_sosedov(mine, s, v):
    """
    Vrni koordinati polja z največ sosednjih min

    Args:
        mine (set of (int, int)): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        tuple of int: koordinati polja

    """

    najvec_min = 0
    najvec_min_koordinate = ()

    for x, y in vsa_polja(s, v):

        naj = sosedov(x, y, mine)

        if naj > najvec_min:
            najvec_min = naj
            najvec_min_koordinate = (x, y)

    if najvec_min_koordinate == ():
        najvec_min_koordinate = (0, 0)
        return  najvec_min_koordinate
    else:
        return najvec_min_koordinate


def brez_sosedov(mine, s, v):
    """
    Vrni množico koordinat polj brez min na sosednjih poljih. Polje samo lahko
    vsebuje mino.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        set of tuple: polja brez min na sosednjih poljih
    """

    m = set()
    for x, y in vsa_polja(s, v):

        if sosedov(x, y, mine) == 0:
            m.add((x, y))

    return m


def po_sosedih(mine, s, v):
    """
    Vrni slovar, katerega ključi so možna števila sosednjih polj z minami
    (torej števila od 0 do 8), vrednosti pa množice koordinat polj s toliko
    sosedami.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        dict: (glej zgoraj)
    """

    slovar = {}

    for i in range(9):
        slovar[i] = set()

    for j in range(9):
        for x, y in vsa_polja(s, v):

            if sosedov(x, y, mine) == j:
                slovar[j].add((x, y))

    return slovar




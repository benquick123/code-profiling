def dolzina_poti(pot):
    """
    Vrni dolžino podane poti, vključno z vmesnimi polji.

    Args:
        pot (list of tuple): seznam koordinat polj

    Returns:
        int: dolžina poti
    """

    return sum([abs(e2[0] - e1[0]) if e2[0] != e1[0] else abs(e2[1] - e1[1]) for e1, e2 in zip(pot, pot[1:])])


def varen_premik(x0, y0, x1, y1, mine):
    """
    Vrni `True`, če je pomik z (x0, y0) and (x1, y1) varen, `False`, če ni.

    Args:
        x0 (int): koordinata x začetnega polja
        y0 (int): koordinata y začetnega polja
        x1 (int): koordinata x končnega polja
        y1 (int): koordinata y končnega polja
        mine (set of tuple of int): koordinate min

    Returns:
        bool: `True`, če je premik varen, `False`, če ni.
    """

    return not (({(x0, e) for e in range(min(y0, y1), max(y0, y1) + 1) if x0 == x1} | {(i, y0) for i in range(min(x0, x1),max(x0, x1) + 1) if y0 == y1}) & mine)




def varna_pot(pot, mine):
    """
    Vrni `True`, če je podana pot varna, `False`, če ni.

    Args:
        pot (list of tuple of int): koordinate točk na poti (brez vmesnih točk)
        mine (set of tuple of int): koordinate min

    Returns:
        bool: `True`, če je pot varna, `False`, če ni.
    """

    return all([True if varen_premik(e1[0], e1[1], e2[0], e2[1], mine) else False for e1, e2 in zip(pot, pot[1:])] + [not (set(pot) & mine)])



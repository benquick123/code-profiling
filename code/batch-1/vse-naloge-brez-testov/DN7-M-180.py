from math import sqrt
def sosedov(x, y, mine):
    mines = 0
    for x1, y1 in mine:
        if x1 in range(x-1, x+2) and y1 in range(y-1, y+2):
                if (x1, y1) != (x, y):
                    mines += 1
    return mines
"""
Vrni število sosedov polja s koordinatami `(x, y)` na katerih je mina.
    Polje samo ne šteje.

    Args:
        x (int): koordinata x
        y (int): koordinata y
        mine (set of tuple of int): koordinate min

    Returns:
        int: število sosedov
"""

def najvec_sosedov(mine, s, v):
    mine_values = {}
    coord_sys = vsa_polja(s, v)   #poklici prvo funkcijo ki ustvari koordinatni sistem
    for a, b in coord_sys:       #za vsako terko v koord. sistemu
        s = sosedov(a, b, mine)  #poklici funkcijo in spremenljivki dodeli stevilo min za
        mine_values[(a, b)] = s     #cisto vsako tocno v sistemu...nato dodaj vse v slovar
    return max(mine_values, key=mine_values.get) #vrne ti key v slovarju z največjim value oz. stevilom min

"""
    Vrni koordinati polja z največ sosednjih min

    Args:
        mine (set of (int, int)): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        tuple of int: koordinati polja

    """

def brez_sosedov(mine, s, v):
    u_safe = set()
    coord_sys = vsa_polja(s, v)
    for a, b in coord_sys:
        c = sosedov(a, b, mine)
        if c == 0:
            u_safe.add((a, b))
    return u_safe

"""
    Vrni množico koordinat polj brez min na sosednjih poljih. Polje samo lahko
    vsebuje mino.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        set of tuple: polja brez min na sosednjih poljih
    """

def po_sosedih(mine, s, v):
    neo = []
    coord_sys = vsa_polja(s, v)
    for i in range(9):
        neo.append(i)
        slovar_min = {k: set() for k in neo}
    for a, b in coord_sys:
        st_min = sosedov(a, b, mine)
        coords = (a, b)
        if st_min in slovar_min:
            slovar_min[st_min].add(coords)
    return slovar_min


def sosedov(x, y, mine):
    """
    Vrni število sosedov polja s koordinatami `(x, y)` na katerih je mina.
    Polje samo ne šteje.

    Args:
        x (int): koordinata x
        y (int): koordinata y
        mine (set of tuple of int): koordinate min

    Returns:
        int: število sosedov
    """
    return sum([1  for x1,y1 in vsa_polja(x+3,y+3) if abs(x1 - x) == 1 and abs(y1-y)<2 or abs(y1 - y) == 1 and  abs(x1-x)<2  if (x1,y1) in mine ])






def najvec_sosedov(mine, s, v):
    """
    Vrni koordinati polja z največ sosednjih min

    Args:
        mine (set of (int, int)): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        tuple of int: koordinati polja

    """
    return [(x, y) for x, y in vsa_polja(s, v) if max([sosedov(x1, y1, mine) for x1, y1 in vsa_polja(s, v)]) == sosedov(x, y, mine)][0]



def brez_sosedov(mine, s, v):
    """
    Vrni množico koordinat polj brez min na sosednjih poljih. Polje samo lahko
    vsebuje mino.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        set of tuple: polja brez min na sosednjih poljih
    """
    return {(x,y) for x,y in vsa_polja(s,v) if sosedov(x,y,mine) == 0}



def po_sosedih(mine, s, v):
    """
    Vrni slovar, katerega ključi so možna števila sosednjih polj z minami
    (torej števila od 0 do 8), vrednosti pa množice koordinat polj s toliko
    sosedami.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        dict: (glej zgoraj)
    """
    return {i:set((x,y) for x,y in vsa_polja(s,v) if sosedov(x,y,mine) == i ) for i in range(9)}

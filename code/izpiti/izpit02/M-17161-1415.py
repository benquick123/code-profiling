from _collections import defaultdict



def bingo(listki,vrstni_red):
    listkic = listki.copy()
    c = -1
    d = len(vrstni_red)
    t = False
    while d != 0:
        if t:
            break
        c += 1
        for i in listkic:
            while len(i) != 0:
                if vrstni_red[c] in i:
                    i.pop(i.index(vrstni_red[c]))
                    if len(i) == 0:
                        t = True
                        break
                d-=1
                break
    e = 0
    for j in listkic:
        if len(j) == 0:
            break
        else:
            e+=1
    return listki[e]





def unikati(s):
    t = []
    for i in range(len(s)):
        if s[i] not in s[:i]:
            t.append(s[i])
    return t

def nogavice(s):
    d = defaultdict(int)
    for ng in s:
        d[ng] += 1
    brez_para = []
    for k,v in d.items():
        if v % 2 != 0:
            brez_para.append(k)
    p = unikati(s)
    nov = []
    for i in p:
        if i in brez_para:
            nov.append(i)
    return nov


class Sledilnik:
    def __init__(self,rezde):
        self.rezde = rezde
        self.sel = 0
        self.d = defaultdict(list)
        for k,v in self.rezde:
            self.d[k].append(v)

    def kje_zivi(self, oseba):
        kje = ""
        for k, v in self.d.items():
            if oseba in v:
                kje = k
                break
        return kje

    def prebivalci(self,kraj):
        return set(self.d[kraj])

    def preseli(self,oseba,kraj):
        kje = ""
        for k,v in self.d.items():
            if oseba in v:
                kje = k
                break
        self.d[kje].pop(self.d[kje].index(oseba))
        self.d[kraj].append(oseba)
        self.sel += 1

    def preseli_vse(self,odkod,kam):
        od = self.d[odkod]
        self.sel += len(od)
        do = self.d[kam]
        for i in od:
            self.d[kam].append(i)
        self.d[odkod].clear()

    def selitev(self):
        return self.sel


def navodila(zaporedje):
    p_list = []
    a = list(zaporedje)
    stevila = []
    for neki in a:
        if neki == "D":
            if len(stevila) > 0:
                p_list.append(int(''.join(stevila)))
            p_list.append(neki)
            stevila = []
        elif neki == "L":
            if len(stevila) > 0:
                p_list.append(int(''.join(stevila)))
            p_list.append(neki)
            stevila = []
        else:
            stevila.append(neki)
    if len(stevila) > 0:
        p_list.append(int(''.join(stevila)))
    return(p_list)


def zamenjano(s, menjave):
    a = []
    for neki in s:
        if neki in menjave:
            a.append(menjave[neki])
        else:
            a.append(neki)
    return a

def zamenjaj(s, menjave):
    for neki in range(0, len(s)):
        if s[neki] in menjave:
            s[neki] = menjave[s[neki]]
        else:
            s[neki] = s[neki]


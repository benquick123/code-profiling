class Carovnik:
    def __init__(self, znam, cena):
        self.caramiz = znam.split(" -> ")[0]
        self.caramv = znam.split(" -> ")[1]
        self.cena = int(cena)
        self.denar = 0

    def caraj(self, s):
        nov = []
        for neki in s:
            if (neki == self.caramiz):
                nov.append(self.caramv)
                self.denar = self.denar + self.cena
            else:
                nov.append(neki)
        return nov

    def zasluzek(self):
        return(self.denar)


class Robot:
    def __init__(self):
        self.x = self.y = 0
        self.smer = 1

    def desno(self):
        self.smer = (self.smer + 1) % 4

    def levo(self):
        self.smer = (self.smer - 1) % 4

    def naprej(self, d):
        if self.smer == 0:
            self.y += d
        elif self.smer == 1:
            self.x += d
        elif self.smer == 2:
            self.y -= d
        else:
            self.x -= d

    def koordinate(self):
        return self.x, self.y

    def razdalja(self):
        return abs(self.x) + abs(self.y)



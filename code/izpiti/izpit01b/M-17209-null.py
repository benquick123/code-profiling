class Carovnik:
    def __init__(self,stvar,cena):
        stvar = stvar.split(" ")
        self.stvar1 = stvar[0]
        self.stvar2 = stvar[2]
        self.cena = cena
        self.zasluzek1 = 0

    def caraj(self,s):
        seznam = []
        for x in s:
            if x == self.stvar1:
                seznam.append(self.stvar2)
                self.zasluzek1 += self.cena
            else:
                seznam.append(x)
        return seznam


    def zasluzek(self):
        return self.zasluzek1



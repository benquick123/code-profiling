def zamenjano(s, menjave):
    s1 = []
    for x in s:
        if x not in menjave:
            s1.append(x)
        else:
            s1.append(menjave[x])
    return s1

def zamenjaj(s, menjave):
    s1 = zamenjano(s, menjave)
    s.clear()
    for x in s1:
        s.append(x)
    return

def navodila(zaporedje):
    s, z = [], ""
    for x in zaporedje:
        if x.isdigit():
            z += x
        if x == "D":
            if z:
                z = int(z)
                s.append(z)
                z = ""
            s.append(x)
        if x == "L":
            if z:
                z = int(z)
                s.append(z)
                z = ""
            s.append(x)
    if zaporedje[-1].isdigit():
        z = int(z)
        s.append(z)
    return s


import collections

def najpogostejse_polje(navodila):
    smeri, usmeritev, x, y, obiski, travel_guide, promet = ["desno", "dol", "levo", "gor"], "desno", 0, 0, [], collections.defaultdict(int), 0
    travel_guide[(0, 0)] = 1
    for komanda in navodila:
        if komanda == "D":
            usmeritev = smeri[(smeri.index(usmeritev) + 1)%4]
        if komanda == "L":
            usmeritev = smeri[(smeri.index(usmeritev) - 1) % 4]
        if komanda.isdigit():
            if usmeritev == "dol":
                y = y - int(komanda)
                obiski.append((x, y))
            if usmeritev == "gor":
                y = y + int(komanda)
                obiski.append((x, y))
            if usmeritev == "desno":
                x = x + int(komanda)
                obiski.append((x, y))
            if usmeritev == "levo":
                x = x - int(komanda)
                obiski.append((x, y))
    for koordinate in obiski:
        travel_guide[koordinate] += 1
    for keys, values in travel_guide.items():
        if values > promet:
            promet = values
    for keys, values in travel_guide.items():
        if values == promet:
            return keys



class Delitelj:
    def __init__(self,k):
        self.k=k
        self.cena=0
    def akcija(self,s):

        nov = []
        for stevilo in s:
            if stevilo%self.k==0:
                self.cena+=stevilo/self.k
                nov.append(stevilo/self.k)
            else:
                nov.append(stevilo)

        s[:]=nov
    def cena(self):
        return self.cena


def migracije(ime_datoteke):
    stevilo=0
    kraj1=""
    kraj2=""
    slovar={}
    for vrstica in open(ime_datoteke):
        vrstica = vrstica.strip()
        stevilo = vrstica[:vrstica.index(":")]
        kraj1 = vrstica[vrstica.index(":") + 2:vrstica.index("-") - 1]
        kraj2 = vrstica[vrstica.index(">") + 1:]
        if kraj2 not in slovar:
            slovar[kraj2]= int(stevilo)
        if kraj2 in slovar:
            slovar[kraj2]= int(slovar[kraj2]) + int(stevilo)
        if kraj1 not in slovar:
            slovar[kraj1] = 0-int(stevilo)
        if kraj1 in slovar:
            slovar[kraj1] = int(slovar[kraj1])- int(stevilo)
    return [min(slovar),max(slovar)]

def roboti(navodila, n):
    kordinate= []
    robot=1
    smer = navodila[robot]
    x,y = 0
    while n>0:
        if smer == "J":
            y -=1
        if smer == "S":
            y += 1
        if smer == "V":
            x+= 1
        if smer == "Z":
            x-=1
        if navodila[robot] + n < len(navodila):
            smer = navodila[robot] + n
    return kordinate

def brez_ponavljanja(s):   #vem da ni pravilna. rekurzija...
    for x in s:
        if x in s[s.index(x)+1:s.index(x)+2]:
            s.remove(x)
    return s

def brez_ponavljanja(s):    #poskus rekurzije...
    i=1
    if s[i] in brez_ponavljanja(s[i+1:]):
        s.remove(s[i])
    return s


class Robot:
    def __init__(self):
        self.x = self.y = 0
        self.smer = 1

    def desno(self):
        self.smer = (self.smer + 1) % 4

    def levo(self):
        self.smer = (self.smer - 1) % 4

    def naprej(self, d):
        if self.smer == 0:
            self.y += d
        elif self.smer == 1:
            self.x += d
        elif self.smer == 2:
            self.y -= d
        else:
            self.x -= d

    def koordinate(self):
        return self.x, self.y

    def razdalja(self):
        return abs(self.x) + abs(self.y)

def zakladi(navodila):
    jaz = Robot()
    zaklad=0
    for a in navodila:
        x, y = jaz.koordinate()
        obiskani=[]
        if (abs(x)+abs(y)) % 7 == 0 and (x,y) not in obiskani:
            zaklad+=1
            obiskani.append((x,y))
        if a == "D":
            jaz.desno()
        if a  == "L":
            jaz.levo()
        else:
            jaz.naprej(a)
    return zaklad

class Delitelj:
    def __init__(self, k):
        self.a = k
        self.cena = 0


    def akcija(self,s):
        for x in s:
            if x % self.a == 0:
                self.cena += (x/self.a)


class Test01Migracije(unittest.TestCase):
    def test_migracije(self):
        import os
        from random import randint
        fn = "m".format(randint(10000, 99999))
        try:
            with open(fn, "wt") as f:
                f.write("""8: Maribor -> Ljubljana
3: Maribor -> Nova Gorica
10: Ljubljana -> Maribor
5: Koper -> Nova Gorica
3: Novo mesto -> Nova Gorica
""")
            self.assertEqual(migracije(fn), ("Maribor", "Nova Gorica"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
3: Mb -> Ng
12: Lj -> Mb
5: Kp -> Ng
3: Nm -> Ng""")
            self.assertEqual(migracije(fn), ("Lj", "Mb"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
3: Mb -> Ng""")
            self.assertEqual(migracije(fn), ("Mb", "Lj"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
9: Ng -> Lj""")
            self.assertEqual(migracije(fn), ("Ng", "Lj"))

            with open(fn, "wt") as f:
                f.write("""11: Maribor -> Lj
5: Lj -> Celje
9: Ng -> Celje""")
            self.assertEqual(migracije(fn), ("Maribor", "Celje"))
        finally:
            os.remove(fn)



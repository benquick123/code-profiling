from collections import defaultdict

def migracije(ime_datoteke):
    datoteka = open(ime_datoteke)

    odslo = dict()
    prislo = dict()

    for vrstica in datoteka:
        st_ljudi = int(vrstica.split(":")[0])
        odkod, kam = [ime_kraja.strip() for ime_kraja in vrstica.split(": ")[1].split(" -> ")]
        if odkod not in odslo:
            odslo[odkod] = 0
        if kam not in prislo:
            prislo[kam] = 0
        odslo[odkod] += st_ljudi
        prislo[kam] += st_ljudi

    datoteka.close()

    max_odslo = 0
    max_odslo_kraj = ""
    for ime in odslo:
        if odslo[ime] > max_odslo:
            max_odslo = odslo[ime]
            max_odslo_kraj = ime

    max_prislo = 0
    max_prislo_kraj = ""
    for ime in prislo:
        if prislo[ime] > max_prislo:
            max_prislo = prislo[ime]
            max_prislo_kraj = ime

    return (max_odslo_kraj, max_prislo_kraj)

class Delitelj:
    def __init__(self, k):
        self.k = k
        self.cena = 0

    def akcija(self, seznam):
        for i in range(len(seznam)):
            if seznam[i] % self.k == 0:
                seznam[i] = seznam[i] / self.k
                self.cena += seznam[i]

def brez_ponavljanja(s):
    doslej = []
    for el in s:
        if not doslej or (doslej and el != doslej[-1]):
            doslej.append(el)
    return doslej

class Robot:
    def __init__(self):
        self.x = self.y = 0
        self.smer = 1

    def desno(self):
        self.smer = (self.smer + 1) % 4

    def levo(self):
        self.smer = (self.smer - 1) % 4

    def naprej(self, d):
        if self.smer == 0:
            self.y += d
        elif self.smer == 1:
            self.x += d
        elif self.smer == 2:
            self.y -= d
        else:
            self.x -= d

    def koordinate(self):
        return self.x, self.y

    def razdalja(self):
        return abs(self.x) + abs(self.y)

def zakladi(navodila):
    robot = Robot()
    zakladna_polja = [(robot.x, robot.y)]

    premiki = {'D': robot.desno, 'L': robot.levo}
    for navodilo in navodila:
        if navodilo in premiki:
            premiki[navodilo]()
        else:
            st_polj_naprej = int(navodilo)
            robot.naprej(st_polj_naprej)
            if (abs(robot.x) + abs(robot.y)) % 7 == 0:
                if (robot.x, robot.y) not in zakladna_polja:
                    zakladna_polja.append((robot.x, robot.y))

    return len(zakladna_polja)

def roboti(navodila, n):
    seznam_robotov = [Robot() for x in range(n)]
    slovar = { 'S': 0, 'V': 1, 'J': 2, 'Z': 3}

    i = 0
    for navodilo in navodila:
        if i > len(seznam_robotov) - 1:
            i = 0
        seznam_robotov[i].smer = slovar[navodilo]
        seznam_robotov[i].naprej(1)
        i += 1

    return [(robot.x, robot.y) for robot in seznam_robotov]

import unittest

class Test01Migracije(unittest.TestCase):
    def test_migracije(self):
        import os
        from random import randint
        fn = "m".format(randint(10000, 99999))
        try:
            with open(fn, "wt") as f:
                f.write("""8: Maribor -> Ljubljana
3: Maribor -> Nova Gorica
10: Ljubljana -> Maribor
5: Koper -> Nova Gorica
3: Novo mesto -> Nova Gorica
""")
            self.assertEqual(migracije(fn), ("Maribor", "Nova Gorica"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
3: Mb -> Ng
12: Lj -> Mb
5: Kp -> Ng
3: Nm -> Ng""")
            self.assertEqual(migracije(fn), ("Lj", "Mb"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
3: Mb -> Ng""")
            self.assertEqual(migracije(fn), ("Mb", "Lj"))

            with open(fn, "wt") as f:
                f.write("""8: Mb -> Lj
9: Ng -> Lj""")
            self.assertEqual(migracije(fn), ("Ng", "Lj"))

            with open(fn, "wt") as f:
                f.write("""11: Maribor -> Lj
5: Lj -> Celje
9: Ng -> Celje""")
            self.assertEqual(migracije(fn), ("Maribor", "Celje"))
        finally:
            os.remove(fn)



from math import *
class Minobot:
    def __init__(self): # konstruktor, oz. ko se minobot pokliče
        self.x = 0 # koordinate so na začetku na 0
        self.y = 0
        self.angle = 0 # na začetku je obrnejen desno, torej mora bit kot enak 0
    def naprej(self, d):
        if self.angle==0 or self.angle%360==0: # če je kot enak 0 ali večkratnik števila 360
            self.x +=d #potem se premikamo v desno po x osi
        if self.angle==180 or self.angle%360==180: # če je kot enak 180 ali je pa kot pri deljenju z 360 enak 180
            self.x -= d # potem se pomikamo v levo po x osi
        if self.angle==90 or self.angle%360==90: # če je kot enak 90 ali je pa kot pri deljenju z 360 enak 90
            self.y += d # potem se pomikamo gor po y osi
        if self.angle==270 or self.angle%360==270: # če je kot enak 270 ali je pa kot pri deljenju z 360 enak 270
            self.y -=d # potem se pomikamo dol po y osi
    def desno(self):
        self.angle += -90 # po koordinatnem sistemu se obrnemo v desno če odštejemo 90
    def levo(self):
        self.angle += +90 # po koordinatnem sistemu se obrnemo v desno če prištejmo 90
    def koordinate(self):
        return self.x, self.y # vrnemo samo koordinate x in y
    def razdalja(self):
        vrednost_x=abs(self.x) # sem spravim koliko je razlika med koordinatami x= 0 in pozicijo x na kateri je minobot
        vrednost_y=abs(self.y) # sem spravim koliko je razlika med koordinatami y= 0 in pozicijo y na kateri je minobot
        skupna_vrednost=vrednost_x+ vrednost_y
        return skupna_vrednost












import unittest


class TestObvezna(unittest.TestCase):
    def test_minobot(self):
        a = Minobot()
        b = Minobot()

        self.assertEqual(a.koordinate(), (0, 0))
        self.assertEqual(b.koordinate(), (0, 0))
        self.assertEqual(a.razdalja(), 0)
        self.assertEqual(b.razdalja(), 0)

        a.naprej(1)
        self.assertEqual(a.koordinate(), (1, 0))
        self.assertEqual(b.koordinate(), (0, 0))
        self.assertEqual(a.razdalja(), 1)
        self.assertEqual(b.razdalja(), 0)

        a.naprej(2)
        self.assertEqual(a.koordinate(), (3, 0))
        self.assertEqual(b.koordinate(), (0, 0))
        self.assertEqual(a.razdalja(), 3)
        self.assertEqual(b.razdalja(), 0)

        b.naprej(2)
        self.assertEqual(a.koordinate(), (3, 0))
        self.assertEqual(b.koordinate(), (2, 0))
        self.assertEqual(a.razdalja(), 3)
        self.assertEqual(b.razdalja(), 2)

        a.desno()  # zdaj je obrnjen dol
        a.naprej(4)
        self.assertEqual(a.koordinate(), (3, -4))
        self.assertEqual(b.koordinate(), (2, 0))
        self.assertEqual(a.razdalja(), 7)
        self.assertEqual(b.razdalja(), 2)

        a.desno()  # obrnjen je levo
        a.naprej(1)
        self.assertEqual(a.koordinate(), (2, -4))
        self.assertEqual(b.koordinate(), (2, 0))
        self.assertEqual(a.razdalja(), 6)
        self.assertEqual(b.razdalja(), 2)

        a.desno()  # obrnjen je gor
        a.naprej(1)
        self.assertEqual(a.koordinate(), (2, -3))
        self.assertEqual(b.koordinate(), (2, 0))
        self.assertEqual(a.razdalja(), 5)
        self.assertEqual(b.razdalja(), 2)

        a.desno()  # obrnjen desno
        a.naprej(3)
        self.assertEqual(a.koordinate(), (5, -3))
        self.assertEqual(b.koordinate(), (2, 0))
        self.assertEqual(a.razdalja(), 8)
        self.assertEqual(b.razdalja(), 2)

        b.levo()  # obrnjen gor
        b.naprej(3)
        self.assertEqual(b.koordinate(), (2, 3))
        self.assertEqual(b.razdalja(), 5)

        b.levo()  # obrnjen levo
        b.naprej(3)
        self.assertEqual(b.koordinate(), (-1, 3))
        self.assertEqual(b.razdalja(), 4)

        a.naprej(5)
        self.assertEqual(a.koordinate(), (10, -3))
        self.assertEqual(a.razdalja(), 13)


class TestDodatna(unittest.TestCase):
    def test_undo(self):
        a = Minobot()
        a.desno()  # gleda dol
        a.naprej(4)
        a.levo()  # gleda desno
        a.naprej(1)
        a.naprej(2)

        self.assertEqual(a.koordinate(), (3, -4))
        a.razveljavi()

def preberi_pot(ukazi):
    pot = [(0,0)]
    x = y = l = d = 0
    smer = 0
    for ukaz in ukazi.split('\n'):
        if ukaz == "DESNO":
            d += 1
        elif ukaz == "LEVO":
            l += 1
        else:
            premik = int(ukaz)
            smer = ((max(d,l)-min(d,l))) % 4
            if d > l and (smer == 1 or smer == 3):
                x += (-(smer-2))*premik
            elif d < l and (smer == 1 or smer == 3):
                x += ((smer-2))*premik
            else:
                if smer == 0:
                    y += -premik
                if smer == 2:
                    y += premik
            pot += [(x,y)]
    return pot


def zapisi_pot(ukazi):
    pot = []
    gledam = smer = premik = 0
    for (x,y),(x1, y1) in zip(ukazi, ukazi[1:]):
        gledam = smer
        if x < x1:
            premik = x1-x
            smer = 1
            if gledam == 2:
                pot += 3*["DESNO"]
            else:
                pot += abs(gledam-smer)*["DESNO"]
            pot += [str(premik)]
        if x > x1:
            premik = abs(x1-x)
            smer = 3
            pot += abs(gledam - smer) * ["DESNO"]
            pot += [str(premik)]
        if y < y1:
            premik = y1-y
            smer = 2
            if gledam == 3:
                pot += 3 * ["DESNO"]
            else:
                pot += abs(gledam - smer) * ["DESNO"]
            pot += [str(premik)]
        if y > y1:
            premik = abs(y1-y)
            smer = 0
            if gledam == 0 or gledam == 2:
                pot += abs(gledam - smer) * ["DESNO"]
            elif gledam == 3:
                pot += ["DESNO"]
            else:
                pot += 3 * ["DESNO"]
            pot += [str(premik)]
    return '\n'.join(pot)



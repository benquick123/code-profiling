def polje_v_mine(polje):
    sirina = 1
    visina = 1
    x = -1
    y = 0
    mine = set()
    for znak in polje:
        if znak == "." or "X":
            x += 1
        if znak == "X":
            mine.add((x,y))
        if znak == " ":
            sirina = x
            y += 1
            x = -1
            visina += 1
    if visina == 1:
        sirina = x+1
    return (mine,sirina,visina)



def polje_v_mine(polje):
    k = set()
    polje = polje.strip()
    polje = polje.split(" ")
    v = len(polje)
    s = len(polje[0])
    for x in range(v):
        for y in range(s):
            if polje[x][y] == "X":
                k.add((y,x))
    return (k,s,v)




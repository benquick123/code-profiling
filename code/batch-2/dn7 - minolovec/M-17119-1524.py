def preberi_pot(ukazi):
    obr = 0
    x = 0
    y = 0
    r = [(x,y)]
    ukazi = ukazi.split('\n')
    for i in ukazi:
        if i == "LEVO":
            if obr == 0:
                obr = 270
            else:
                obr -= 90
        elif i == "DESNO":
            if obr == 270:
                obr = 0
            else:
                obr += 90
        elif i.isnumeric():
            i = int(i)
            if obr == 0:
                y += i
                r.append((abs(x), abs(y)))
            if obr == 90:
                x += i
                r.append((abs(x), abs(y)))
            if obr == 180:
                y -= i
                r.append((abs(x), abs(y)))
            if obr == 270:
                x -= i
                r.append((abs(x),abs(y)))
    return r

def zapisi_pot(pot):
    uk = ""
    pre = 0
    for i in range(1, len(pot)):
        x = pot[i - 1][0]
        y = pot[i - 1][1]
        x1 = pot[i][0]
        y1 = pot[i][1]
        if (y < y1):
            uk += str(y1-y)
            uk += "\n"
        if (y > y1):
            uk += "LEVO\nLEVO\n"
            uk += str(y - y1)
            uk += "\n"
            uk += "LEVO\nLEVO\n"
        if (x < x1):
            uk += "DESNO\n"
            uk += str(x1 - x)
            uk += "\n"
            uk += "DESNO\nDESNO\nDESNO\n"
        if (x > x1):
            uk += "LEVO\n"
            uk += str(x - x1)
            uk += "\n"
            uk += "LEVO\nLEVO\nLEVO\n"
    return uk





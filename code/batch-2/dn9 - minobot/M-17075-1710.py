def opisi_stanje_2(x, y, smer):
    if smer == "N":
        direction = "^"
    elif smer == "S":
        direction = "v"
    elif smer == "E":
        direction = ">"
    elif smer == "W":
        direction = "<"
    return ("{d:} {x:>4}:{y:}".format(x = "(" + str(x), y = str(y) + ")", d = direction))


def opisi_stanje(x, y, smer):
    if smer == "N":
        direction = "^"
    elif smer == "S":
        direction = "v"
    elif smer == "E":
        direction = ">"
    elif smer == "W":
        direction = "<"
    return ("{x:>3}:{y:<3} {d:}".format(x = x, y = y, d = direction))

###

def prevedi(ime_vhoda, ime_izhoda):
    t = open(ime_izhoda, "w")
    for entry in izvedi(ime_vhoda):
        x = opisi_stanje(entry[0], entry[1], entry[2])
        t.write(x + "\n")
    t.close()


def sosedi(doslej, zemljevid): #{1, 3, 7}, zemljevid} ->>> {4,6,11,8}
    connections = []

    genesis = zemljevid
    for conn in doslej:
        for sosed in genesis[conn]:
            if sosed not in doslej:
                connections.append(sosed)
    return set(connections)


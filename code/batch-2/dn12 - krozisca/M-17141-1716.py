def order_by_sequence(x): #dobi [1,8,7,6,4]
    minimum = min(x) #1
    index = x.index(minimum) #dobi index od 1

    y = x[index:] #pred 1
    y.extend(x[:index]) #za 1
    return y



def preberi(ime_datoteke):
    # preberem stevilo krozisc
    counter = 0
    for vrstica in open(ime_datoteke, encoding='utf8'):
        this_line = vrstica.strip()
        counter += 1



    # inicializacija dict z vsemi specificiranimi krozisci
    cions = {}
    for i in range(1, counter + 1):
        cions[i] = []

    # vzpostavitev in povezava krozisc s conn
    inic = 1
    for vrstica in open(ime_datoteke, encoding='utf8'):

        this_line = vrstica.strip()
        for cifra in this_line.split():
            cions[inic].append(int(cifra))
        cions[inic] = order_by_sequence(cions[inic])
        inic += 1

    return cions

def mozna_pot(pot, zemljevid): #[1, 3, 7, 3, 4, 2]
    endings = []
    success = False
    genesis = zemljevid
    if len(genesis[pot[0]]) == 1 and len(genesis[pot[-1]]) == 1:
        success = True
    else:
        return False

    for key in genesis:
        if len(genesis[key]) == 1:
            endings.append(key)

    for con in pot[1:-1]:
        if con in endings:
            return False

    for path in pot:
        try:
            if pot[pot.index(path)+1] in genesis[path]:
                success = True
            else:
                return False
        except: pass

    return success

def hamiltonova(pot, zemljevid):

    endings = []
    krozisca = []
    for key in zemljevid:
        if len(zemljevid[key]) == 1:
            endings.append(key)
    for key in zemljevid:
        krozisca.append(key)
    #iz krozisc odstranim vse endinge
    pathways = [x for x in krozisca if x not in endings]

    if mozna_pot(pot, zemljevid) is True:
        #iz nase poti odstranim vse ending
        set_path = [x for x in pot if x not in endings]
        if sorted(pathways) == sorted(set_path):
            return True
        else:
            return False


def order_by_input(from_lane, current_lane, zemljevid): #dobi 1, dobi 3, dobi povezave od 3
    genesis = zemljevid
    index = genesis.index(from_lane) #index hrani index mesto
    y = genesis[index:]
    y.extend(genesis[:index])
    return y


def calculate_exit(previous, current, cycles, zemljevid): #intro lane, now lane, cycles
    genesis = zemljevid
    i=0
    for lane in order_by_input(previous, current, genesis): #1, 8, 7, 6, 4
        if i == cycles: return order_by_input(previous, current, genesis)[i]
        else: i = i + 1

def prevozi(zacetek, navodila, zemljevid):
    genesis = zemljevid
    endings = []
    for key in genesis:
        if len(genesis[key]) == 1:
            endings.append(key)

    instructions = []
    instructions.append(zacetek)
    next_up = genesis[zacetek][0]
    instructions.append(next_up)



    for cycles in navodila:

        exit_to = calculate_exit(zacetek, next_up, cycles, genesis[next_up])
        instructions.append(exit_to)
        zacetek = next_up
        next_up = exit_to

        if next_up in endings: return instructions


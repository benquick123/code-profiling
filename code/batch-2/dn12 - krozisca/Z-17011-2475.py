def obrni(izvozi):
    razporejeni = []
    najmanjsi = 10000
    for element in izvozi:
        if element < najmanjsi:
            najmanjsi = element
    for element in izvozi:
        if element == najmanjsi:
            razporejeni.append(element)
        elif razporejeni:
            razporejeni.append(element)
    if len(razporejeni) != len(izvozi):
        for element in izvozi:
            if element != razporejeni[0]:
                razporejeni.append(element)
            else:
                break
    return razporejeni


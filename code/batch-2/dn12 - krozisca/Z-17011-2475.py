def sosedi(doslej, zemljevid):
    zraven = set()
    for vozlisce in doslej:
        for krozisce in zemljevid[vozlisce]:
            zraven.add(krozisce)
    return zraven-doslej
def razdalja(x, y, zemljevid):
    stevec = 1
    if not y in zemljevid[x]:
        for krozisce in zemljevid[x]:
            stevec += razdalja(krozisce, y, zemljevid)
    else:
        return stevec

def razdalja(x, y, zemljevid):
    zraven = {x}
    stevec = 1
    while y not in zraven:
        for krozisca in zraven:
            vsi_sosedi = sosedi(zraven, zemljevid)
        for sosed in vsi_sosedi:
            zraven.add(sosed)
        if y not in zraven:
            stevec += 1
    return stevec




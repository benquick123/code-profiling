def preberi(ime_datoteke):
    n = 1
    slovar = {}
    datoteka = open(ime_datoteke)
    for tocka in datoteka:
        seznam = []
        for povezava in tocka.split():
            seznam += [int(povezava)]
        a = 0
        for x in seznam:
            if x == min(seznam):
                od = a
            a += 1
        slovar[n] = seznam[od:] + seznam[:od]
        n += 1
    return slovar

def mozna_pot(pot, zemljevid):
    n = 0
    for mesto1, mesto2 in zip(pot, pot[1:]):
        if mesto2 not in zemljevid[mesto1]:
            return False
        if mesto1 == mesto2:
            return False
        if len(zemljevid[mesto1]) < 2 and not (n == 0 or n == len(pot)):
            return False
        n += 1
    if len(zemljevid[pot[0]]) > 1 or len(zemljevid[pot[-1]]) > 1:
        return False
    return True

def hamiltonova(pot, zemljevid):
    if not mozna_pot(pot, zemljevid):
        return False
    seznam_krozisc = []
    for i in range(1,len(zemljevid) + 1):
        if len(zemljevid[i]) > 1:
            seznam_krozisc += [i]
    if not (len(pot)-2) == len(seznam_krozisc):
        return False
    for mesto in pot:
        if pot.count(mesto) > 1:
            return False
    return True

def navodila(pot, zemljevid):
    navodila = []
    prej = pot[0]
    for zdaj, potem in zip(pot[1:],pot[2:]):
        n = 1
        for x in zemljevid[zdaj]:
            if x == prej:
                a = n
            if x == potem:
                b = n
            n += 1
        if b > a:
            zavoj = b-a
        else:
            zavoj = len(zemljevid[zdaj]) - (a-b)
        if prej == potem:
            zavoj = 0
        navodila += [zavoj]
        prej = zdaj
    return navodila

def prevozi(zacetek, navodila, zemljevid):
    voznja = [zacetek]
    prej = zacetek
    zdaj = zemljevid[zacetek][0]
    for i in navodila:
        voznja += [zdaj]
        n = 0
        for x in zemljevid[zdaj]:
            if x == prej:
                pre = n
            n += 1
        potem = zemljevid[zdaj][(pre+i)%len(zemljevid[zdaj])]
        prej = zdaj
        zdaj = potem
    return voznja+[zdaj]

def sosedi(doslej, zemljevid):
    vse_povezave = set()
    for x in doslej:
        for y in zemljevid[x]:
            vse_povezave.add(y)
    return vse_povezave - doslej

def razdalja(x, y, zemljevid):
    razdal = 1
    mnozica_sosedov = sosedi({x},zemljevid)
    while y not in mnozica_sosedov:
        mnozica_sosedov.union(sosedi(mnozica_sosedov, zemljevid))
        mnozica_sosedov = sosedi(mnozica_sosedov, zemljevid)
        razdal += 1
    return razdal


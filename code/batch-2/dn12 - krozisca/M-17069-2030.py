def preberi(ime_datoteke):
    vhod = open(ime_datoteke, "r")
    slovar = {}
    nov = []
    for b in vhod:
        nov.append(b.split())       #da se znebimo vsega shita, ki ga branje iz txt datoteke namece

    i = 1           #stevc za kljuce slovarja
    for a in nov:
        b = []
        for c in a:                     #vrednosti seznama a, ki so string, shranimo kot int v nov seznam b
            b.append(int(c))

        g = b.index(min(b))         #v g se shrani indeks od najmanjse vrednosti v b (min ne dela s stringi)
        ns = []
        h = 0
        while h < 2:                    #nacin za sortiranje od min naprej:
            while g < len(b):
                if b[g] not in ns:
                    ns.append(b[g])
                g += 1
            h += 1
            g = 0

        slovar[i] = ns
        i += 1
    return slovar

def mozna_pot(pot, zemljevid):
    dolzina = len(pot) -1
    if len(zemljevid[pot[0]]) != 1 or len(zemljevid[pot[dolzina]]) != 1:
        return False

    pot2 = pot[1:-1]        #ce gre vmes ven
    for a in pot2:
        if len(zemljevid[a]) == 1 :
            return False

    c = 1
    for a in pot:               #ce se krizisce ponovi
        if c < len(pot):
            if a == pot[c]:
                return False
            c += 1

    i = 1
    for a in pot:        #ce ni povezave:
        if i<len(pot):
            if pot[i] not in zemljevid[a]:
                return False
        i += 1
    return True

def hamiltonova(pot, zemljevid):
    if not (mozna_pot(pot,zemljevid)):
        return False

    ponovitve = []
    for a in pot:
        if a not in ponovitve:
            ponovitve.append(a)
        else:
            return False

    krozisca = []
    for a in zemljevid:
        if len(zemljevid[a]) != 1:
            krozisca.append(a)

    for a in krozisca:
        if a not in pot:
            return False
    return True

def navodila(pot, zemljevid):
    navodila = []
    for i, a in enumerate(pot[1:-1]):
        seznam = zemljevid[a]
        prejsnja = pot[i]
        if i+1<len(pot[1:]):
            naslednja = pot[1:][i+1]

        i_prejsnje = seznam.index(prejsnja)
        i_naslednje = seznam.index(naslednja)

        razlika = i_naslednje - i_prejsnje
        izvoz = razlika % len(seznam)
        navodila.append(izvoz)
    return navodila

def prevozi(zacetek, navodila, zemljevid):
    pot = []
    pot.append(zacetek)
    a = zemljevid[zacetek]
    for b in a:
        pot.append(b)

    for i, a in enumerate(navodila):
        seznam = zemljevid[pot[i+1]]
        prejsnji = pot[i]

        i_prejsnji = seznam.index(prejsnji)
        i_naslednji = (i_prejsnji + a) - len(seznam)
        naslednji = seznam[i_naslednji]

        pot.append(naslednji)
    return pot


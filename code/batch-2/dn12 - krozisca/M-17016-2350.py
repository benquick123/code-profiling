def sosedi(doslej, zemljevid):

    povezani = []

    for krozisce in doslej:
        povezave = zemljevid.get(krozisce)
        for p in povezave:
            if p not in doslej:
                povezani.append(p)

    sos = set(povezani)
    return sos

def razdalja(x, y, zemljevid):

    st = 0

    mnozica = {x}

    while y not in mnozica:
        mnozica.update(sosedi(mnozica, zemljevid))
        st += 1

    return st


def mozna_pot(pot, zemljevid):
    i=1
    seznam_koncev_in_zacetkov=[]
    stevilo_elementov={}
    for x,vsebina in zemljevid.items():
        stevilo_elementov[x]=len(vsebina)
        if stevilo_elementov[x]==1:
            seznam_koncev_in_zacetkov.append(x)
    for krozisce in pot:
        naslednje_krizisce = pot[i]
        i = i + 1
        koncnu_krizisce = pot[-1]
        sosedi = zemljevid[krozisce]
        if pot[0] not in seznam_koncev_in_zacetkov:
            return False
        if naslednje_krizisce in sosedi:
            if naslednje_krizisce == koncnu_krizisce:
                if naslednje_krizisce in seznam_koncev_in_zacetkov:
                    return True
                else:
                    return False
            if naslednje_krizisce in seznam_koncev_in_zacetkov:
                return False
        else:
            return False


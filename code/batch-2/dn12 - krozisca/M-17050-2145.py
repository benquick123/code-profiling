def spremeni(seznam):
    nov1=[]

    nov=seznam

    for el in seznam:
        if el!=min(seznam):
            nov.append(el)
        if el==min(seznam):
            break

    preveri = False
    for st in nov:
        if st==min(seznam):
            preveri=True
        if preveri:
            nov1.append(st)

    return nov1



def preberi(ime_datoteke):
    datoteka = open(ime_datoteke)
    slovar = {}
    stevec=1
    for vrstica in datoteka:
        vr = vrstica.strip()
        seznam = vr.split(" ")

        sezna = list(map(int, seznam))

        nov = spremeni(sezna)

        slovar[stevec]=nov
        stevec=stevec+1

    datoteka.close()
    return slovar


def dolzina_poti(pot):
    """
    Vrni dolžino podane poti, vključno z vmesnimi polji.

    Args:
        pot (list of tuple): seznam koordinat polj

    Returns:
        int: dolžina poti
    """

    if pot != []:
        previous_y = pot[0][1]
        previous_x = pot[0][0]
    else:
        previous_y = 0
        previous_x = 0
    count = 0
    for coords in pot:
        if coords[1] != previous_y:
            count += abs(coords[1] - previous_y)
        previous_y = coords[1]
    for coords in pot:
        if coords[0] != previous_x:
            count += abs(coords[0] - previous_x)
        previous_x = coords[0]
    return abs(count)



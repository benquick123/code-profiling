def sosedov(x, y, mine):
    """
    Vrni število sosedov polja s koordinatami `(x, y)` na katerih je mina.
    Polje samo ne šteje.

    Args:
        x (int): koordinata x
        y (int): koordinata y
        mine (set of tuple of int): koordinate min

    Returns:
        int: število sosedov
    """

    count = 0
    for coords in mine:
        x1 = (x - coords[0])
        y1 = (y - coords[1])
        if x1 >= -1 and x1 <= 1 and y1 >= -1 and y1 <= 1 and coords != (x, y):
            count += 1
    return count


def najvec_sosedov(mine, s, v):
    """
    Vrni koordinati polja z največ sosednjih min

    Args:
        mine (set of (int, int)): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        tuple of int: koordinati polja
    """

    max_sosedi = 0
    result = (0, 0)
    for polje in vsa_polja(s, v):
        sosedi = sosedov(polje[0], polje[1], mine)
        if sosedi > max_sosedi:
            max_sosedi = sosedi
            result = polje
    return result

def brez_sosedov(mine, s, v):
    """
    Vrni množico koordinat polj brez min na sosednjih poljih. Polje samo lahko
    vsebuje mino.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        set of tuple: polja brez min na sosednjih poljih
    """

    result = set()
    for i in vsa_polja(s, v):
        if sosedov(i[0], i[1], mine) == 0:
            result.add(i)
    return result

def po_sosedih(mine, s, v):
    """
    Vrni slovar, katerega ključi so možna števila sosednjih polj z minami
    (torej števila od 0 do 8), vrednosti pa množice koordinat polj s toliko
    sosedami.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        dict: (glej zgoraj)
    """

    result = {}
    for i in range(9):
        result[i] = set()
    for key in result:
        for polje in vsa_polja(s, v):
            if sosedov(polje[0], polje[1], mine) == key:
                result[key].add(polje)
    return result



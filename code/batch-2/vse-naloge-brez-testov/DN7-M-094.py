
def vsa_polja(s, v):
    return ((x, y) for x in range(s) for y in range(v))



def sosedov(x, y, mine):
    c = 0
    for a, b in mine:
        if a in range(x - 1, x + 2) and b in range(y - 1, y + 2) and (a,b) != (x,y):
            c += 1
    return c



def najvec_sosedov(mine, s, v):
    naj_sosedov = 0
    koordinate = (0, 0)

    for a, b in vsa_polja(s, v):
        if sosedov(a, b, mine) > naj_sosedov:
            koordinate = a, b
            naj_sosedov = sosedov(a, b, mine)
    return koordinate








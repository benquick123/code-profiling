mine = {(3, 0), (1, 1), (6, 1), (1, 2), (2, 2), (6, 3)}

def sosedov(x, y, mine):
    """
    Vrni število sosedov polja s koordinatami `(x, y)` na katerih je mina.
    Polje samo ne šteje.

    Args:
        x (int): koordinata x
        y (int): koordinata y
        mine (set of tuple of int): koordinate min

    Returns:
        int: število sosedov
    """
    a = 0
    for (x1, y1) in mine:
        if ((x1 in range(x - 1, x + 2)) and (y1 in range(y - 1, y + 2)) and (x1, y1) != (x, y)):
            a = a + 1
    return a


def najvec_sosedov(mine, s, v):
    """
    Vrni koordinati polja z največ sosednjih min

    Args:
        mine (set of (int, int)): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        tuple of int: koordinati polja

    """
    polja = vsa_polja(s, v)
    naj_bomb = 0
    k = (0, 0)
    for x, y in polja:
        if sosedov(x, y, mine) > naj_bomb:
            naj_bomb = sosedov(x, y, mine)
            k = (x, y)
    return k


def brez_sosedov(mine, s, v):
    """
    Vrni množico koordinat polj brez min na sosednjih poljih. Polje samo lahko
    vsebuje mino.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        set of tuple: polja brez min na sosednjih poljih
    """
    polja = vsa_polja(s, v)
    k = (0, 0)
    s = set()
    for (x, y) in polja:
        if sosedov(x, y, mine) == 0:
            k = (x, y)
            s.add((x, y))
    return s


def po_sosedih(mine, s, v):
    """
    Vrni slovar, katerega ključi so možna števila sosednjih polj z minami
    (torej števila od 0 do 8), vrednosti pa množice koordinat polj s toliko
    sosedami.

    Args:
        mine (set of tuple of int): koordinate min
        s (int): širina polja
        v (int): višina polja

    Returns:
        dict: (glej zgoraj)
    """
    k = {}
    for i in range(0, 9):
        k[i] = set()
    for x, y in vsa_polja(s, v):
        k[sosedov(x, y, mine)].add((x, y))
    return k



import unittest

def unikati(s):
    novseznam = []
    for i in s:
        if i not in novseznam:
            novseznam.append(i)
    return novseznam

def avtor(tvit):
    return tvit.split(":")[0]

def vsi_avtorji(tviti):
    avtorji=[]
    for i in tviti:
        avtorji.append(avtor(i))
    return unikati(avtorji)

def izloci_besedo(beseda):
    nealfa = 0
    while not beseda[nealfa].isalnum():
        nealfa=nealfa+1
    beseda = beseda[nealfa:]

    nealfa = len(beseda)-1
    while not beseda[nealfa].isalnum():
        nealfa = nealfa - 1
    beseda = beseda[:nealfa+1]

    return beseda

def se_zacne_z(tvit, c):
    seznam = tvit.split()
    nov = []
    for i in seznam:
        if i[0]==c:
            nov.append(izloci_besedo(i))
    return nov

def zberi_se_zacne_z(tviti, c):
    seznam = []
    for i in tviti:
        seznam = seznam + se_zacne_z(i, c)
    return unikati(seznam)

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, "@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    seznam = vsi_avtorji(tviti) + vse_afne(tviti)
    return sorted(unikati(seznam))


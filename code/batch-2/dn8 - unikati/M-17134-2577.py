def premik(ukaz, x, y, smer):
    smeri = "NESW"
    premiki = [(0, -1), (1, 0), (0, 1), (-1, 0)]
    ismer = smeri.index(smer)
    if ukaz == "R":
        smer = smeri[(ismer + 1) % 4]
    elif ukaz == "L":
        smer = smeri[(ismer - 1) % 4]
    else:
        dx, dy = premiki[ismer]
        x += dx * ukaz
        y += dy * ukaz
    return x, y, smer

def izvedi(ime_datoteke):
        x = 0
        y = 0
        smer = "N"
        sez = [(0, 0, "N")]
        datoteka = open(ime_datoteke, "r")
        tekst = datoteka.readlines()
        for vsebina in tekst:
            vsebina = vsebina.strip()
            if "DESNO" in vsebina:
                pozicija = premik("R", x, y, smer)
                x, y, smer = pozicija
                sez.append((x, y, smer))
            elif "LEVO" in vsebina:
                pozicija = premik("L", x, y, smer)
                x, y, smer = pozicija
                sez.append((x, y, smer))
            elif "NAPREJ" in vsebina:
                neki = (vsebina.split(" "))
                pozicija = premik(int(neki[1]), x, y, smer)
                x, y, smer = pozicija
                sez.append((x, y, smer))
        datoteka.close()
        return sez


def opisi_stanje(x, y, smer):
    spremenjenaSmer = ""
    if smer == 'N':
        spremenjenaSmer = '^'
    elif smer == 'E':
        spremenjenaSmer = '>'
    elif smer == 'S':
        spremenjenaSmer = 'v'
    elif smer == 'W':
        spremenjenaSmer = '<'
    return ("{:>3}:{:<4}{}".format(str(x), str(y), spremenjenaSmer))


def prevedi(ime_vhoda, ime_izhoda):
    seznam = izvedi(ime_vhoda)
    with open(ime_izhoda, 'w') as datoteka:
        for x, y, smer in seznam:
            datoteka.write(opisi_stanje(x, y, smer) + '\n')
    datoteka.close()


import unittest
class TestObvezna(unittest.TestCase):
    def test_branje(self):
        self.assertEqual(
            izvedi("primer.txt"),
            [(0, 0, 'N'), (0, 0, 'E'), (12, 0, 'E'), (12, 0, 'S'), (12, 2, 'S'),
             (12, 2, 'E'), (15, 2, 'E'), (15, 2, 'N'), (15, 2, 'W')]
        )
        self.assertEqual(
            izvedi("ukazi.txt"),
            [(0, 0, 'N'), (0, 0, 'E'), (1, 0, 'E'), (1, 0, 'S'), (1, 0, 'W'),
             (0, 0, 'W'), (0, 0, 'S'), (0, 0, 'E'), (1, 0, 'E'), (1, 0, 'S'),
             (1, 3, 'S'), (1, 3, 'E'), (2, 3, 'E'), (2, 3, 'S'), (2, 3, 'W')]
        )

    def test_opisi_stanje(self):
        self.assertEqual(opisi_stanje(0, 12, "N"), "  0:12  ^")
        self.assertEqual(opisi_stanje(111, 0, "E"), "111:0   >")
        self.assertEqual(opisi_stanje(-2, 111, "S"), " -2:111 v")
        self.assertEqual(opisi_stanje(0, 0, "W"), "  0:0   <")

    def test_prevedi(self):
        from random import randint
        import os
        ime = "izhod{:05}.txt".format(randint(0, 99999))
        try:
            self.assertIsNone(prevedi("primer.txt", ime))
            self.assertEqual(open(ime).read().rstrip(), """  0:0   ^
  0:0   >
 12:0   >
 12:0   v
 12:2   v
 12:2   >
 15:2   >
 15:2   ^
 15:2   <""")

            self.assertIsNone(prevedi("ukazi.txt", ime))
            self.assertEqual(open(ime).read().rstrip(), """  0:0   ^
  0:0   >
  1:0   >
  1:0   v
  1:0   <
  0:0   <
  0:0   v
  0:0   >
  1:0   >
  1:0   v
  1:3   v
  1:3   >
  2:3   >
  2:3   v
  2:3   <""")
        finally:
            os.remove(ime)

        vime = "vhod{:05}.txt".format(randint(0, 99999))
        open(vime, "wt").write("NAPREJ 23\nLEVO\nNAPREJ 17\n")
        try:
            self.assertIsNone(prevedi(vime, ime))
            self.assertEqual(open(ime).read().rstrip(), """  0:0   ^
  0:-23 ^
  0:-23 <
-17:-23 <""")
        finally:
            os.remove(ime)
            os.remove(vime)



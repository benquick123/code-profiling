def se_poznata(tviti, oseba1, oseba2):
    for t in tviti:
        a = avtor(t)
        o = se_zacne_z(t, "@")
        if oseba1 == a and oseba2 in o or \
                oseba2 == a and oseba1 in o:
            return True
    return False

def besedilo(tvit):
    bes = tvit[tvit.find(":")+2:]
    return bes

def zadnji_tvit(tviti):
    slovar = {}
    for t in tviti:
        slovar[avtor(t)] = besedilo(t)
    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for t in tviti:
        if avtor(t) not in slovar:
            slovar[avtor(t)] = besedilo(t)
    return slovar

def prestej_tvite(tviti):
    slovar = {}
    avtorji = []
    for t in tviti:
        avtorji.append(avtor(t))
    for a in avtorji:
        if a not in slovar:
            slovar[a] = 0
        slovar[a] += 1
    return slovar

def omembe(tviti):
    slovar = {}
    for t in tviti:
        auth = t.split(": ")[0]
        omemba = re.findall("@([aA-zZ]*)", t)
        slovar[auth] = slovar.get(auth, []) + omemba
    return slovar

def neomembe(ime, omembe):
    self_omembe = omembe.get(ime, []) + [ime]
    return list(set(omembe.keys()) - set(self_omembe))



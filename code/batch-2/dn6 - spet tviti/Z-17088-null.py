# 1.
def se_poznata(ime1, ime2, omembe):
    x = omembe.get(ime1,[])
    y = omembe.get(ime2,[])
    if (ime2 in x) or (ime1 in y):
        return True
    return False

# 2.
def hashtagi(tviti):
    slovar = {}
    for tvit in tviti:
        hastagi = se_zacne_z(tvit, "#")
        ime = avtor(tvit)
        for hastag in hastagi:
            if hastag not in slovar:
                slovar[hastag] = []
            slovar[hastag].append(ime)
            slovar[hastag].sort()
    return slovar


# Obvezna naloga

# 1.
def besedilo(tvit):
    return tvit[tvit.find(":")+2:]

# 2.
def zadnji_tvit(tviti):
    d = {}
    for e in tviti[::-1]:
        kljuc = e[:e.find(":")]
        vrednost = besedilo(e)
        if kljuc not in d:
            d[kljuc]=vrednost
    return d

# 3.
def prvi_tvit(tviti):
    d = {}
    for e in tviti:
        kljuc = e[:e.find(":")]
        vrednost = besedilo(e)
        if kljuc not in d:
            d[kljuc]=vrednost
    return d


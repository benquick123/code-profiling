def avtor(tvit):
    return tvit.split(":")[0]

def izloci_besedo(beseda):
    print(beseda)
    for i in range(len(beseda)):
        if beseda[i].isalnum():
            beseda = beseda[i:]
            break
    for i in range(len(beseda)-1, 0, -1):
        if beseda[i].isalnum():
            beseda = beseda[:i+1]
            break
    return beseda




def besedilo(tvit):
    return tvit.split(": ", 1)[1]

def zadnji_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        avtor_ = avtor(tvit)
        slovar[avtor_] = besedilo(tvit)
    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        avtor_ = avtor(tvit)
        if avtor_ not in slovar:
            slovar[avtor_] = besedilo(tvit)
    return slovar

def prestej_tvite(tviti):
    slovar = {}
    for tvit in tviti:
        avtor_ = avtor(tvit)
        if avtor_ not in slovar:
            slovar[avtor_] = 0
        slovar[avtor_] += 1
    return slovar


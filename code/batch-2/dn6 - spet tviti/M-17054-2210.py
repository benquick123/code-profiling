def unikati(s):
    unik = []
    for i in s:
        if i in unik:
            continue
        unik.append(i)
    return unik

def avtor(tvit):
    avt = tvit.split(":")[0]
    return avt

def vsi_avtorji(tviti):
    va = []
    for tvit in tviti:
        avt = tvit.split(":")[0]
        if avt in va:
            continue
        va.append(avt)
    return va

def izloci_besedo(beseda):
    for i in beseda:
        if i.isalnum():
            break
        beseda = beseda.replace(i, "")
    for i in beseda[::-1]:
        if i.isalnum():
            break
        beseda = beseda.replace(i, "")
    return beseda

def se_zacne_z(tvit, c):
    szz = tvit.split()
    words_with_c = []
    for i in szz:
        if i.startswith(c):
            words_with_c.append(izloci_besedo(i))
    return words_with_c

def zberi_se_zacne_z(tviti, c):
    words_with_c = []
    for tvit in tviti:
        szz = tvit.split()
        for i in szz:
            if i.startswith(c) and izloci_besedo(i) not in words_with_c:
                words_with_c.append(izloci_besedo(i))
    return words_with_c

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, "@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    osebe = vsi_avtorji(tviti)
    handles = vse_afne(tviti)
    for i in handles:
        if i not in osebe:
            osebe.append(i)
    return sorted(osebe)

def besedilo(tvit):
   twt = tvit
   return "".join(twt.split(":", 1)[1:]).strip()

def zadnji_tvit(tviti):
    twts = {}
    for tvit in tviti:
        twts[avtor(tvit)] = besedilo(tvit)
    return twts

def prvi_tvit(tviti):
    twts = {}
    for tvit in tviti:
        if avtor(tvit) not in twts:
            twts[avtor(tvit)] = besedilo(tvit)
    return twts

def prestej_tvite(tviti):
    twts = collections.defaultdict(int)
    for tvit in tviti:
        twts[avtor(tvit)] += 1
    return twts

def omembe(tviti):
    twts = {}
    for tvit in tviti:
        if avtor(tvit) not in twts:
            twts[avtor(tvit)] = se_zacne_z(tvit, "@")
        else:
            twts[avtor(tvit)] += se_zacne_z(tvit, "@")
    return twts


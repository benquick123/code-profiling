import unittest
import collections

def besedilo(tvit):
    kopiraj = False
    nb = ""
    for znak in tvit:
        if kopiraj:
            nb += znak
        if znak == ":":
            kopiraj = True
    return nb[1:]

def zadnji_tvit(tviti):
    s = {}
    for tvit in tviti:
        pisec = avtor(tvit)
        bes = besedilo(tvit)
        s[pisec] = bes
    return s

def prvi_tvit(tviti):
    s = {}
    for tvit in tviti[::-1]:
        pisec = avtor(tvit)
        bes = besedilo(tvit)
        s[pisec] = bes
    return s

def prestej_tvite(tviti):
    s = collections.defaultdict(int)
    for tvit in tviti:
        pisec = avtor(tvit)
        s[pisec] += 1
    return s

def omembe(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        pisec = avtor(tvit)
        omenjeni = se_zacne_z(tvit, "@")
        s[pisec] += omenjeni
    return s

def neomembe(ime, omembe):
    ns = []
    for k in omembe:
        if(k != ime and k not in omembe[ime]):
            ns.append(k)
    return ns

def se_poznata(ime1, ime2, omembe):
    for k in omembe:
        if k == ime1 and ime2 in omembe[k]:
            return True
        if k == ime2 and ime1 in omembe[k]:
            return True
    return False

def hashtagi(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        hashtags = se_zacne_z(tvit,"#")
        pisec = avtor(tvit)
        for hashtag in hashtags:
            s[hashtag] = sorted(s[hashtag] + [pisec])
    return s

#funkcije iz prejšnje naloge
def unikati(s):
    ns = []
    for a in s:
        if not a in ns:
            ns += [a]
    return ns

def avtor(tvit):
    avtor = ""
    for znak in tvit:
        if znak == ":":
            break
        avtor += znak
    return avtor

def vsi_avtorji(tviti):
    seznam_avtorjev = []
    for tvit in tviti:
        seznam_avtorjev += [avtor(tvit)]
    seznam_avtorjev = unikati(seznam_avtorjev)
    return seznam_avtorjev

def izloci_besedo(beseda):
    i = 0
    for znak in beseda:
        if str.isalnum(znak):
            break
        i += 1
    beseda = beseda[i:]

    i = 0
    for znak in beseda[::-1]:
        if str.isalnum(znak):
            break
        i += 1
    beseda = beseda[:-i+len(beseda)]
    return beseda

def se_zacne_z(tvit,c):
    tvit = tvit.split(" ")
    ns = []
    for beseda in tvit:
        if beseda[0] == c:
            ns += [izloci_besedo(beseda)]
    return ns

def zberi_se_zacne_z(tviti, c):
    ns = []
    for tvit in tviti:
        ns += se_zacne_z(tvit, c)
    return unikati(ns)

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, "@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    ns = unikati(vse_afne(tviti) + vsi_avtorji(tviti))
    return sorted(ns)

def custva(tviti, hashtagi):
    ns = []
    for tvit in tviti:
        for hashtag in hashtagi:
            if hashtag in se_zacne_z(tvit,"#"):
                ns += [avtor(tvit)]
    return sorted(unikati(ns))




import unittest

def avtor(tvit):
    tvit=tvit.split(":")
    return tvit[0]

def izloci_besedo(beseda):
    while beseda and not beseda[0].isalnum():
        beseda = beseda[1:]
    while beseda and not beseda[-1].isalnum():
        beseda = beseda[:-1]
    return beseda

def vse_afne(tvit):
    s=[]
    for a in tvit.split():
        if a[0]=="@":
            a=izloci_besedo(a)
            if a not in s:
                s.append(a)
    return s

def unikati(s):
    a=[]
    for x in s:
        if x not in a:
            a.append(x)
    return a

def vsi(tviti):
    s=[]
    for tvit in tviti:
        for a in tvit.split():
            if a[0]=="@":
                a=izloci_besedo(a)
                if a not in s:
                    s.append(a)
    return s

def vsi_hashtagi(tviti):
    s=[]
    for tvit in tviti:
        for a in tvit.split():
            if a[0]=="#":
                a=izloci_besedo(a)
                if a not in s:
                    s.append(a)
    return s

def besedilo(tvit):
    tvit=tvit.split(": ", 1)
    return tvit[1]

def zadnji_tvit(tviti):
    a={}
    for tvit in tviti:
        b=avtor(tvit)
        a[b]=besedilo(tvit)
    return a

def prvi_tvit(tviti):
    a = {}
    for tvit in tviti:
        b = avtor(tvit)
        if b not in a:
            a[b] = besedilo(tvit)
    return a

def prestej_tvite(tviti):
    a={}
    for tvit in tviti:
        b=avtor(tvit)
        if b in a:
            a[b]+=1
        else:
            a[b]=1
    return a

def omembe(tviti):
    a={}
    for tvit in tviti:
        b=avtor(tvit)
        c=vse_afne(tvit)
        if b in a:
            a[b]+=c
        else:
            a[b]=c
    return a

def neomembe(ime, omembe):
    a=[]
    b=[]
    for oseba in omembe:
        if oseba==ime:
            b+=omembe[oseba]
    for oseba in omembe:
        if oseba not in b:
            if oseba!=ime:
                a.append(oseba)
    return a

def se_poznata(ime1, ime2, omembe):
    for key in omembe:
        if key==ime1:
            if ime2 in omembe[key]:
                return True
        if key==ime2:
            if ime1 in omembe[key]:
                return True

def hashtagi(tviti):
    a={}
    b=unikati(vsi_hashtagi(tviti))
    for htag in b:
        a[htag]=[]
    for tvit in tviti:
        for beseda in tvit.split():
            if izloci_besedo(beseda) in a:
                a[izloci_besedo(beseda)].append(avtor(tvit))
                a[izloci_besedo(beseda)]=sorted(a[izloci_besedo(beseda)])
    return a


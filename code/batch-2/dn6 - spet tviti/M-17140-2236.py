def besedilo(tvit):
    a=tvit.split(':')[1:]
    return ':'.join(a).strip()

def zadnji_tvit(tviti):
    s=collections.defaultdict()
    for tvit in tviti:
        s[avtor(tvit)]=besedilo(tvit)
    return s

def prvi_tvit(tviti):
    s=collections.defaultdict()
    oseba=[]
    for tvit in tviti:
        if avtor(tvit) not in oseba:
            s[avtor(tvit)] = besedilo(tvit)
            oseba.append(avtor(tvit))
    return s

def prestej_tvite(tviti):
    s=collections.defaultdict(int)
    for tvit in tviti:
        s[avtor(tvit)]+=1
    return s

def omembe(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        a = tvit.split()
        os = avtor(tvit)
        if os not in s:
            s[os] = []
        for b in a:
            if b[0] == '@':
                i = izloci_besedo(b)
                s[os].append(i)

    return s

def neomembe(ime, omembe):
    neom = []
    o = []
    for name in omembe:
        o.append(name)
    for oseba in o:
        if oseba not in omembe[ime] and oseba != ime:
            neom.append(oseba)
    return neom





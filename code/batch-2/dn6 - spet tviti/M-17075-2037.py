def besedilo(tweet):
    a = tweet.split(": ", 1)
    return " ".join(a[1:])


def zadnji_tvit(tweets):
    result = {}
    for tweet in tweets:
        entry = besedilo(tweet)
        key = tweet.split(":")[0]
        result[key] = entry
    return result


def prvi_tvit(tweets):
    result = {}
    for tweet in tweets:
        entry = besedilo(tweet)
        key = tweet.split(":")[0]
        if key not in result:
            result[key] = entry
    return result


def prestej_tvite(tweets):
    temp = {}
    result = {}
    for tweet in tweets:
        entry = besedilo(tweet)
        key = tweet.split(":")[0]
        if key not in temp:
            temp[key] = [entry]
        else:
            temp[key].append(entry)
    for key in temp:
        lenght = len(temp[key])
        result[key] = lenght
    return result


def omembe(tweets):
    temp_words = []
    result = {}
    for tweet in tweets:
        key = tweet.split(":")[0]
        words = besedilo(tweet).split()
        if key not in result:
            result[key] = temp_words
        for word in words:
            if word.startswith("@"):
                word = izloci_besedo(word)
                if result[key] == []:
                    result[key] = [word]
                else:
                    result[key].append(word)
    return result


def neomembe(ime, omembe):
    mentions = []
    for key in omembe:
        if key not in omembe[ime] and key != ime:
            mentions.append(key)
    return mentions



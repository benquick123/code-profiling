def se_poznata(ime1, ime2, omembe):
    if ime1 in omembe.get(ime2, "0") or ime2 in omembe.get(ime1, "0"):
        return True
    else:
        return False


def hashtagi(tweets):
    result = {}
    authors = []
    for tweet in tweets:
        tweet_words = besedilo(tweet).split()
        for word in tweet_words:
            if word.startswith("#"):
                for tweet in tweets:
                    if word in tweet:
                        authors.append(tweet.split(":")[0])
                key = izloci_besedo(word)
                authors.sort()
                result[key] = authors
                authors = []
    return result



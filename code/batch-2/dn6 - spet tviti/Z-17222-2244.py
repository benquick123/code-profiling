import collections

def besedilo (tvit):
    tekst = tvit.split(":")[1:]
    return ":".join(tekst).strip()

def zadnji_tvit(tviti):
    sl = collections.defaultdict()
    for e in tviti:
        sl[avtor(e)] = besedilo(e)
    return sl

def prvi_tvit(tviti):
    prvi = []
    sl = collections.defaultdict()
    for e in tviti:
        if avtor(e) not in prvi:
            sl[avtor(e)] = besedilo(e)
            prvi.append(avtor(e))
    return sl

def prestej_tvite(tviti):
    sl = collections.defaultdict(int)
    for e in tviti:
        sl[avtor(e)] +=1
    return sl

def omembe (tviti):
    sl = collections.defaultdict(list)
    for e in tviti:
        tekst = e.split()
        a = avtor(e)
        if a not in sl:
            sl[a]= []
        for i in tekst:
            if i[0]=="@":
                ime=izloci_besedo(i)
                sl[a].append(ime)
    return sl

def neomembe(ime, omembe):
    s = []
    ne = []
    for e in omembe:
        s.append(e)
    for i in s:
        if i not in omembe[ime] and ime != i:
            ne.append(i)
    return ne





def se_poznata(ime1, ime2, omembe):
    for a in omembe:
        if a == ime1:
            for b in omembe[a]:
                if ime2 == b:
                    return True
        if a == ime2:
            for b in omembe[a]:
                if ime1 == b:
                    return True
    return False

def hashtagi(tviti):
    s = collections.defaultdict(list)
    for tvit in tviti:
        s[se_zacne_z(tvit, "#")[0]] += [avtor(tvit)]
        if len(se_zacne_z(tvit, "#")) > 1:
            s[se_zacne_z(tvit, "#")[1]] += [avtor(tvit)]
    t = {x:sorted(s[x]) for x in s.keys()}
    return t



def unikati(s):
    t = []
    [t.append(i) for i in s if not t.count(i)]
    return t

def avtor(tvit):
    return tvit[:tvit.find(':')]

def vsi_avtorji(tviti):
    s = []
    for tvit in tviti:
        s.append(avtor(tvit))
    return s

def izloci_besedo(beseda):
    start = 0
    for crka in beseda:
        if crka.isalnum():
            break
        start += 1
    end = len(beseda)
    for crka in reversed(beseda):
        if crka.isalnum():
            break
        end -= 1
    return beseda[start:end]

def se_zacne_z(tvit, c):
    t = []
    s = tvit.split()
    for beseda in s:
        if beseda[0] == c:
            t.append(izloci_besedo(beseda))
    return t

def vsi_hashtagi(tviti):
    t = []
    for tvit in tviti:
        t += se_zacne_z(tvit, '#')
    return unikati(t)


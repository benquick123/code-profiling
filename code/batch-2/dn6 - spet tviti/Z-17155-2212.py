def unikati(s):
    seznamNov = []
    for e in s:
        if e not in seznamNov:
            seznamNov.append(e)
    return seznamNov

def avtor(tvit):
    return tvit.split(':')[0]

def izloci_besedo(beseda):
    for i in range(len(beseda)):
        if beseda[i].isalnum():
            start = i
            break
    for i in range(len(beseda)):
        if beseda[(len(beseda)-1)-i].isalnum():
            end = i
            break
    if end == 0:
        return beseda[start:]
    else:
        return beseda[start:-end]

def se_zacne_z(tvit, c):
    novSeznam = []
    for rijec in tvit.split(' '):
        if rijec[0] == c:
            novSeznam.append(izloci_besedo(rijec))
    return novSeznam

def zberi_se_zacne_z(tviti, c):
    seznam = []
    for tvit in tviti:
        rijec = se_zacne_z(tvit, c)
        seznam.extend(rijec)
    return unikati(seznam)

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, '@')

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, '#')


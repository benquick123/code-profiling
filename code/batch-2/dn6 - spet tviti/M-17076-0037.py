def unikati(s):
    sez = []
    for num in s:
        if num not in sez:
            sez.append(num)
    return sez

def avtor(tvit):
    return tvit.split(":")[0]

def vsi_avtorji(tviti):
    sez = []
    for a in tviti:
        sez.append(avtor(a))
    return unikati(sez)

def izloci_besedo(beseda):
    b = 0
    c = len(beseda)
    while not beseda[b].isalnum():
        b += 1
    while not beseda[c - 1].isalnum():
        c -= 1
    return beseda[b : c]

def se_zacne_z(tvit, c):
    sez = tvit.split(" ")
    sez2 = []
    for word in sez:
        if word[0] == c:
            sez2.append(izloci_besedo(word))
    return sez2

def zberi_se_zacne_z(tviti, c):
    besede = []
    for tvit in tviti:
        besede2 = se_zacne_z(tvit, c)
        for beseda in besede2:
            if beseda not in besede:
                besede.append(beseda)
    return besede

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, "@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    osebe = []
    for tvit in tviti:
        avtor2 = avtor(tvit)
        if avtor2 not in osebe:
            osebe.append(avtor2)
    ostale_osebe = vse_afne(tviti)
    for oseba in ostale_osebe:
        if oseba not in osebe:
            osebe.append(oseba)
    osebe.sort()
    return osebe

def besedilo(tvit):
    return tvit.split(": ", 1)[1]

def zadnji_tvit(tviti):
    d = {}
    for tvit in tviti:
        d[avtor(tvit)] = besedilo(tvit)
    return d

def prvi_tvit(tviti):
    d = {}
    for tvit in tviti:
        if avtor(tvit) in d:
            continue
        d[avtor(tvit)] = besedilo(tvit)
    return d

def prestej_tvite(tviti):
    d = {}
    for tvit in tviti:
        if avtor(tvit) in d:
            d[avtor(tvit)] += 1
        else:
            d[avtor(tvit)] = 1
    return d

def omembe(tviti):
    d = {}
    for tvit in tviti:
        sez = [tvit]
        if avtor(tvit) not in d:
            d[avtor(tvit)] = vse_afne(sez)
        else:
            d[avtor(tvit)].extend(vse_afne(sez))
    return d

def neomembe(ime, omembe):
    sez = []
    sez_imen = omembe.keys()
    sez_omemb = omembe[ime]
    for name in sez_imen:
        if name == ime:
            continue
        if name not in sez_omemb:
            sez.append(name)
    return sez



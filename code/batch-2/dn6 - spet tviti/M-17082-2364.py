import unittest
import collections

# Naloga: Spet tviti s slovarji (OK)

def besedilo(tvit):
	s = tvit
	for x in tvit:
		if x != ":":
			s = s[1:]
		else:
			return s[2:]

def oseba(tvit):
	s = ""
	for x in tvit:
		if x != ":":
			s += x
		else:
			return s

def zadnji_tvit(tviti):
	s = collections.defaultdict()
	for tvit in tviti:
		s[oseba(tvit)] = besedilo(tvit)
	return s

def prvi_tvit(tviti):
	s = collections.defaultdict()
	for tvit in tviti:
		if not oseba(tvit) in s:
			s[oseba(tvit)] = besedilo(tvit)
	return s

def prestej_tvite(tviti):
	s = collections.defaultdict(int)
	for tvit in tviti:
		s[oseba(tvit)] += 1
	return s

def izloci_besedo(beseda):
	if beseda.isalnum():
		return beseda
	else:
		s = ""
		for x in beseda:
			if x.isalnum() or x == "-":
				s += x
		return s

def poisci(tvit, c):
	tvit2 = tvit.split()
	s = []
	for x in tvit2:
		if x[0] == c:
			s.append(izloci_besedo(x))
	return s

def omembe(tviti):
	s = collections.defaultdict()
	for tvit in tviti:
		if not oseba(tvit) in s:
			s[oseba(tvit)] = poisci(tvit, "@")
		else:
			for x in poisci(tvit, "@"):
				s[oseba(tvit)].append(x)
	return s

def neomembe(ime, omembe):
	s = []
	for x in omembe:
		if x != ime and x not in omembe[ime]:
			s.append(x)
	return s

def se_poznata(ime1, ime2, omembe):
	if ime1 in omembe:
		if ime2 in omembe[ime1]:
			return True
	if ime2 in omembe:
		if ime1 in omembe[ime2]:
			return True
	return False

def hashtagi(tviti):
	s = collections.defaultdict(list)
	for tvit in tviti:
		a = poisci(tvit, "#")
		for x in a:
			s[x].append(oseba(tvit))
			s[x].sort()
	return s




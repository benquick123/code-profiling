def besedilo(tvit):
    for x in tvit:
        if x == ":":
            st = tvit.index(x) + 2
            break

    return tvit[st:]

def zadnji_tvit(tviti):
    slovar = {}

    for x in tviti:
        slovar[avtor(x)] = besedilo(x)

    return slovar

def prvi_tvit(tviti):
    slovar = {}

    for x in tviti:
        if avtor(x) not in slovar:
            slovar[avtor(x)] = besedilo(x)

    return slovar

def prestej_tvite(tviti):
    slovar = {}

    for x in tviti:
        if avtor(x) not in slovar:
            slovar[avtor(x)] = 1
        else:
            slovar[avtor(x)] += 1

    return slovar


def omembe(tviti):
    slovar = collections.defaultdict(str)

    for x in tviti:
        if avtor(x) not in slovar:
            slovar[avtor(x)] = se_zacne_z(x,"@")
        else:
            slovar[avtor(x)].extend(se_zacne_z(x,"@"))

    return slovar


def neomembe(ime,omembe):
    seznam = []

    for x in omembe:
        if x not in omembe[ime]:
            seznam.append(x)

    seznam.remove(ime)
    return seznam



def besedilo(tvit):
    output=""
    i=0
    for znak in tvit:
        i+=1
        if znak==":":
            output=tvit[i+1:]
            break
    return output

def avtor(tvit):
    output=""
    i=0
    for znak in tvit:
        i+=1
        if znak==":":
            output=tvit[:i-1]
            break
    return output


def zadnji_tvit(tviti):
    output={}
    for tvit in tviti:
        output[avtor(tvit)]=besedilo(tvit)
    return output

def prvi_tvit(tviti):
    output={}
    for tvit in tviti:
        if avtor(tvit) not in output:
            output[avtor(tvit)]=besedilo(tvit)
    return output

def prestej_tvite(tviti):
    output={}
    for tvit in tviti:
        if avtor(tvit) not in output:
            output[avtor(tvit)]=1
        elif avtor(tvit) in output:
            output[avtor(tvit)]+=1
    return output


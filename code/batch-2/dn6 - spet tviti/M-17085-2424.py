def besedilo(tvit):
    vrednost=tvit.split(": ",1)
    return vrednost[1]

def avtorji(tvit):
    avtor=tvit.split(": ", 1)
    return avtor[0]

def zadnji_tvit(tviti):
    slovar=collections.defaultdict(str)#slovar ki ima znotraj neko privzeto vrednost ""
    for i in tviti:
        tvit=besedilo(i)
        avtor=avtorji(i)
        slovar[avtor]=tvit
    return slovar

def prvi_tvit(tviti):
    slovar = collections.defaultdict(str)  # slovar ki ima znotraj neko privzeto vrednost
    for i in tviti:
        tvit = besedilo(i)
        avtor = avtorji(i)
        if slovar[avtor] == "":
            slovar[avtor] = tvit
    return slovar

def prestej_tvite(tviti):
    slovar = collections.defaultdict(int)  # slovar ki ima znotraj neko privzeto vrednost
    for i in tviti:
        avtor = avtorji(i)
        slovar[avtor]+=1
    return slovar


def omembe(tviti):
    slovar= collections.defaultdict(list)

    for i in tviti:
        sezacnez=se_zacne_z(i, "@")
        avtor=avtorji(i)
        slovar[avtor].extend(sezacnez)
    return slovar


def neomembe(ime, omembe):
    vsi_avt= list(omembe.keys())
    imena = []
    for k, v in omembe.items():
        if k == ime:
            imena = v
    ele = list(set(vsi_avt) - set(imena))
    ele.remove(ime)
    return ele


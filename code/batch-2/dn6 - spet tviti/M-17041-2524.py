import unittest
import collections

def besedilo(tvit):
    niz = ""
    start = False
    for e in tvit:
        if start:
            niz += e
        if e == ":":
            start = True
    return niz.lstrip(" ")

def zadnji_tvit(tviti):
    slovar = dict()
    for tvit in tviti:
        k = tvit.split(":")[0]
        v = besedilo(tvit)
        slovar[k] = v
    return slovar

def prvi_tvit(tviti):
    slovar = dict()
    for tvit in tviti:
        k = tvit.split(":")[0]
        v = besedilo(tvit)
        if k not in slovar and v not in slovar:
            slovar[k] = v
    return slovar

def prestej_tvite(tviti):
    lst = []
    for tvit in tviti:
        k = tvit.split(":")[0]
        lst.append(k)
    stevilo_tvitov = collections.Counter(lst)
    return stevilo_tvitov



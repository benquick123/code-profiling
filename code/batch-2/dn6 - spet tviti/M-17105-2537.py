import collections

def besedilo(tvit):
    return tvit.split(": ", 1)[1]

def zadnji_tvit(tviti):
    slovar = {}
    for tvit in tviti:
        slovar[avtor(tvit)] = besedilo(tvit)
    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for tvit in tviti[::-1]:
        slovar[avtor(tvit)] = besedilo(tvit)
    return slovar

def prestej_tvite(tviti):
    return collections.Counter(vsi_avtorji(tviti))

def omembe(tviti):
    slovar = {}
    for tvit in tviti:
        oznacene = se_zacne_z(tvit, "@")
        pisec = avtor(tvit)
        if pisec in slovar:
            slovar[pisec] += oznacene
        else:
            slovar[pisec] = oznacene
    return slovar

def neomembe(ime, omembe):
    osebe = []
    for d in omembe:
        if d != ime and d not in omembe[ime]:
            osebe.append(d)
    return osebe

def se_poznata(ime1, ime2, omembe):
    if ime1 in omembe and ime2 in omembe:
        prva = omembe[ime1]
        druga = omembe[ime2]
        return (ime2 in prva or ime1 in druga)
    return False

def hashtagi(tviti):
    slovar = collections.defaultdict(list)
    for tvit in tviti:
        oznaka = se_zacne_z(tvit, "#")
        pisec = avtor(tvit)
        for tag in oznaka:
            if tag not in slovar:
                slovar[tag].append(pisec)
            else:
                slovar[tag].append(pisec)
    for d in slovar:
        slovar[d] = sorted(slovar[d])
    return slovar


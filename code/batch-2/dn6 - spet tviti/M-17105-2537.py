import re
def unikati(s):
    seznam = []
    for x in s:
        if not (x in seznam):
            seznam.append(x)
    return seznam

def avtor(tvit):
    return tvit.split(":")[0]

def vsi_avtorji(tviti):
    avtorji = []
    for tvit in tviti:
        avtorji.append(avtor(tvit))
    return avtorji

def izloci_besedo(beseda):
    isci = re.search('((?!-)[\w-]+(\w))',beseda)
    return isci.group(0)                            

def se_zacne_z(tvit, c):
    tvit = tvit.split()
    besede = []
    for beseda in tvit:
        if(beseda.startswith(c)):
            besede.append(izloci_besedo(beseda))
    return besede

def zberi_se_zacne_z(tviti, c):
    besede=[]
    beseda = [besede.extend(se_zacne_z(tvit, c)) for tvit in tviti]
    return unikati(besede)

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti,"@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    oznacene_osebe = vse_afne(tviti)
    avtorji = vsi_avtorji(tviti)
    osebe = unikati(oznacene_osebe + avtorji)
    return sorted(osebe)


import unittest
import collections

def unikati(s):
    uniq = []
    for x in s:
       if x not in uniq:
           uniq.append(x)
    return uniq

def avtor(tvit):
    s = tvit.split(":")
    return s[0]

def vsi_avtorji(tviti):
    avtorji = []
    for vrstica in tviti:
       avtorji.append(avtor(vrstica))
    return unikati(avtorji)

def izloci_besedo(beseda):

    while not beseda[0].isalnum():
        beseda = beseda[1:]
    while not beseda[-1].isalnum():
        beseda = beseda[:-1]

    return beseda

def se_zacne_z(tvit, c):
    return [izloci_besedo(beseda) for beseda in tvit.split(" ") if beseda[0] ==c ]

def zberi_se_zacne_z(tviti, c):
    s = []
    for tvit in tviti:
         s += se_zacne_z(tvit,c)
    return unikati(s)

def vse_afne(tviti):
    return zberi_se_zacne_z(tviti, "@")

def vsi_hashtagi(tviti):
    return zberi_se_zacne_z(tviti, "#")

def vse_osebe(tviti):
    return sorted(unikati(vse_afne(tviti)+vsi_avtorji(tviti )))

def custva(tviti, hashtagi):
    avtorji = []
    for tvit in tviti:
        for hashtag in hashtagi:
            if tvit.__contains__(hashtag):
                avtorji.append(avtor(tvit))

    return sorted(unikati(avtorji))

def se_poznata1(tviti, oseba1, oseba2):
    for tvit in tviti:
        a = avtor(tvit)
        if a == oseba1:
            if tvit.__contains__("@" + oseba2):
                return True
        if a == oseba2:
            if tvit.__contains__("@" + oseba1):
                return True

    return False

def besedilo(tvit) :
    return ":".join(tvit.split(":")[1:]).strip()

def zadnji_tvit(tviti):
    t = {}

    for tvit in tviti:
        t[avtor(tvit)] = besedilo(tvit)
    return t

def prvi_tvit(tviti):
    t = {}
    for tvit in tviti:
        if not t.get(avtor(tvit)):
            t[avtor(tvit)] = besedilo(tvit)

    return t

def prestej_tvite(tviti):
    pogostosti = collections.defaultdict(int)
    for tvit in tviti:
        pogostosti[avtor(tvit)] += 1
    return pogostosti

def omembe(tviti):
    t = collections.defaultdict(list)
    for tvit in tviti:
        t[avtor(tvit)] += vse_afne([tvit])
    return t

def neomembe(ime,omembe):
    neomenjene_osebe = []
    for vrstica in omembe:
        if avtor(vrstica) not in omembe[ime]:
            if avtor(vrstica) != ime:
                neomenjene_osebe.append(avtor(vrstica))
    return neomenjene_osebe

def se_poznata(ime1, ime2, omembe):
    if ime1 not in omembe or ime2 not in omembe:
        return False
    return ime2 in omembe[ime1] or ime1 in omembe[ime2]

def hashtagi(tviti):
    hashtags = collections.defaultdict(list)
    for tvit in tviti:
        bula = se_zacne_z(tvit,"#")
        for hash in bula:
            hashtags[hash] = sorted(hashtags[hash]+[avtor(tvit)])
    return hashtags




import unittest


def avtor(tvit):
    s = tvit.split(":")
    return s[0]


def unikati(s):
    z = []
    for x in s:
        if x not in z:
            z.append(x)
    return z


def vsi_avtorji(tviti):
    avtorji = []
    for tvit in tviti:
        s = tvit.split(":")
        avtorji.append(s[0])
    return unikati(avtorji)



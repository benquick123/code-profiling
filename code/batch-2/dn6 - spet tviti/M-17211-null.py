def izloci_besedo(beseda):
    while not beseda[0].isalnum():
        beseda = beseda[1:]
    while not beseda[len(beseda)-1].isalnum():
        beseda = beseda[:len(beseda)-1]
    return beseda


def se_zacne_z(tvit, c):
    tvit = tvit.split()
    z = []
    for beseda in tvit:
        if beseda[0] == c:
            z.append(izloci_besedo(beseda))
    return z


def besedilo(tvit):
    b = False
    s = ""
    for c in tvit:
        if c == " " and not b:
            b = True
            continue
        if b:
            s += c
    return s


def zadnji_tvit(tviti):
    sl = {}
    for tvit in tviti:
        sl[avtor(tvit)] = besedilo(tvit)
    return sl


def prvi_tvit(tviti):
    sl = {}
    for tvit in tviti:
        if avtor(tvit) not in sl.keys():
            sl[avtor(tvit)] = besedilo(tvit)
    return sl


def prestej_tvite(tviti):
    sl = {}
    for tvit in tviti:
        if avtor(tvit) in sl.keys():
            sl[avtor(tvit)] += 1
        else:
            sl[avtor(tvit)] = 1
    return sl


def omembe(tviti):
    sl = {}
    for tvit in tviti:
        if avtor(tvit) in sl.keys():
            sl[avtor(tvit)] += se_zacne_z(tvit, "@")
        elif se_zacne_z(tvit, "@"):
             sl[avtor(tvit)] = se_zacne_z(tvit, "@")
        else:
            sl[avtor(tvit)] = []
    return sl



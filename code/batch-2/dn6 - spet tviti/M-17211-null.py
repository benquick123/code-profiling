def hashtagi(tviti):
    h = {}
    for tvit in tviti:
        if se_zacne_z(tvit, "#"):
            hashi = se_zacne_z(tvit, "#")
            for has in hashi:
                if has not in h.keys():
                    h[has] = []
# {'dougcajt': [], 'programiranje1': [], 'krneki': [], 'luft': [], 'zalosten': [], 'split': []}
    for tvit in tviti:
        pisec = avtor(tvit)
        hashi = se_zacne_z(tvit, "#")
        for has in hashi:
            h[has] += [pisec]
    for k in h:
        h[k] = sorted(h[k])
    return h



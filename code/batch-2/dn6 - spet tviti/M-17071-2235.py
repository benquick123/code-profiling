        besede = tvit.split()
        for beseda in besede:
            if beseda[0] == "#":
                hashtag = beseda[1:]
                while not hashtag[0].isalnum():
                    hashtag = hashtag[1:]
                while not hashtag[-1].isalnum():
                    hashtag = hashtag[:-1]
                if hashtag not in hashes:
                    hashes[hashtag] = [oseba]

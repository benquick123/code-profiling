def besedilo(tvit):
    tvit=tvit.split(': ',1)[1]
    return tvit

def zadnji_tvit(tviti):
    slovar={}
    for t in tviti:
        avtor=t.split(': ',1)[0]
        text=t.split(': ',1)[1]
        slovar[avtor]=text
    return slovar

def prvi_tvit(tviti):
    slovar = {}
    for t in tviti:
        avtor = t.split(': ', 1)[0]
        text = t.split(': ', 1)[1]
        if (avtor not in slovar):
            slovar[avtor] = text
    return slovar

def prestej_tvite(tviti):
    slovar={}
    for t in tviti:
        avtor = t.split(': ', 1)[0]
        if(avtor in slovar):
            x=slovar[avtor]+1
            slovar[avtor] = x
        else:
            slovar[avtor]=1
    return slovar


import unittest


def unikati(s):
    tmp = []
    for i in s:
        if i not in tmp:
            tmp.append(i)
    return tmp

def avtor(tvit):
    besede = tvit.split()
    author = besede[0]
    author = list(author)
    author.pop()
    author = ''.join(author)
    return author

def izloci_besedo(beseda):
    # string razibjem v list
    beseda = list(beseda)
    # dokler prvi element v listu beseda ni alfanumerična ponavljaj
    while not(beseda[0].isalnum()):
        # izbriši prvi element lista ( ker ni alfanumeričen )
        del(beseda[0])
    # isti princip kot zgornja zanka, samo da preverjam od odzadaj
    while not(beseda[-1].isalnum()):
        del(beseda[-1])
    beseda = ''.join(beseda)
    return beseda

def se_zacne_z(tvit, c):
    tvit = tvit.split()
    besede_s_pravo_zacetnico = []
    for beseda in tvit:
        if beseda[0] == c:
            besede_s_pravo_zacetnico.append(izloci_besedo(beseda))
    return besede_s_pravo_zacetnico

def vsi_hashtagi(tviti):
    hashtagi =[]
    for tvit in tviti:
        hashtagi += se_zacne_z(tvit, '#')
    hashtagi = unikati(hashtagi)
    return hashtagi

def custva(tviti, hashtagi):
    avtorji = []
    for tvit in tviti:
        hashtag = se_zacne_z(tvit, '#')
        for h in hashtag:
            if h in hashtagi:
                avtorji.append(avtor(tvit))

    avtorji = list(set(avtorji))
    avtorji.sort()
    return avtorji

def besedilo(tvit):
    a = tvit
    a = a.split()
    del(a[0])
    a = ' '.join(a)
    return a

def zadnji_tvit(tviti):
    slovar = dict()
    for tvit in tviti:
        avtor = tvit.split()[0]
        avtor = avtor[:-1]
        slovar[avtor] = besedilo(tvit)
    return slovar

def prvi_tvit(tviti):
    slovar = dict()
    for tvit in tviti:
        avtor = tvit.split()[0]
        avtor = avtor[:-1]
        if slovar.get(avtor) == None:
            slovar[avtor] = besedilo(tvit)
    return slovar

def prestej_tvite(tviti):
    slovar = dict()
    for tvit in tviti:
        avtor = tvit.split()[0]
        avtor = avtor[:-1]
        slovar[avtor] = slovar.get(avtor, 0)
        slovar[avtor] += 1
    return slovar

def omembe(tviti):
    slovar = dict()
    for tvit in tviti:
        avtor = tvit.split()[0]
        avtor = avtor[:-1]
        slovar[avtor] = slovar.get(avtor, list())
        slovar[avtor] += se_zacne_z(tvit, '@')
    return slovar

def neomembe(ime, mentions):
    a = []
    b = mentions[ime]
    c = []
    a = list(mentions.keys())
    for afne in a:
        if afne != ime:
            if afne not in b:
                c.append(afne)
    return c

def se_poznata(ime1, ime2, mentions):

    if ime1 in mentions and ime2 in mentions:
        if ime1 in mentions[ime2]:
            return True
        if ime2 in mentions[ime1]:
            return True
    return False

def hashtagi(tviti):
    slovar = dict()
    for tagi in vsi_hashtagi(tviti):
        slovar[tagi] = custva(tviti, tagi)
    return slovar


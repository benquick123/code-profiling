def se_poznata(ime1, ime2, omembe):
    for i in omembe:
        if i == ime1 and ime2 in omembe[i] or i == ime2 and ime1 in omembe[i]:
            return True
    return False

def hashtagi(tviti):
    a = {}
    for i in vsi_hashtagi(tviti):
        if i not in a:
            a[i] = [avtor(x) for x in tviti if i in x]
        else:
            a[i] += [avtor(x) for x in tviti if i in x]
    for key in a:
        a[key] = sorted(a[key])
    return a


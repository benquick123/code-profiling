def unikati(s):
    xs = []
    for e in s:
        if e not in xs:
            xs.append(e)
    return(xs)
def avtor(tvit):
    xs = tvit.split(':')
    for x in xs:
        return x
def vsi_avtorji(tviti):
    tviti = [tviti.split(':',1)[0] for tviti in tviti]
    xy = []
    for x in tviti:
        if x not in xy:
            xy.append(x)
    return xy
def izloci_besedo(beseda):
  return ''.join(x for x in beseda if x == '-' or x.isalnum())
def se_zacne_z(tvit, c):
    xs = []
    for x in tvit.split():
        if c in x and x[-1].isalnum():
            xs.append(x[1:])
        if c in x and x[-1].isalnum() is False:
            xs.append(x[1:-1])
    return xs
def zberi_se_zacne_z(tviti,c):
    xs = []
    xy = []
    for x in tviti:
        x = x.split()
        for y in x:
            if c in y and y not in xs and y[-1].isalnum():
                xs.append(y[1:])
            if c in y and y[-1].isalnum() is False and y not in xs:
                xs.append(y[1:-1])
        for z in xs:
            if z not in xy:
                xy.append(z)
    return xy

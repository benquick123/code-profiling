def besedilo(tvit):
    return tvit.split(": ", 1)[1]

def zadnji_tvit(tviti):
    zt = {}
    for x in tviti:
        zt[avtor(x)] = besedilo(x)
    return zt

def prvi_tvit(tviti):
    pt = {}
    for x in tviti:
        if avtor(x) not in pt:
            pt[avtor(x)] = besedilo(x)
    return pt


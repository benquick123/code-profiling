def avtor(tvit):
    return tvit.split(":")[0]

def besedilo(tvit):
    return tvit.split(": ",1)[1]

def zadnji_tvit(tviti):
    b = {}
    for tvit in tviti:
        b[avtor(tvit)]=besedilo(tvit)
    return b

def prvi_tvit(tviti):
    b = dict()
    for tvit in tviti:
        if avtor(tvit) not in  b:
            b[avtor(tvit)] = besedilo(tvit)
    return b

def prestej_tvite(tviti):
    b = dict()
    stev = 0
    for tvit in tviti:
        a = avtor(tvit)
        if a not in b:
            for tvit2 in tviti:
                if avtor(tvit2) == a:
                    stev+=1
            b[a] = stev
        stev = 0
    return b

def izloci_besedo(beseda):
    nova = ""
    for  i in range(0,len(beseda)):
        if beseda[i].isalnum()  or beseda[i] == "-":
            nova+=beseda[i]
    return nova

def vse_afne(tvit):
    tmp = []
    c="@"
    tvit = tvit.split(" ")
    for beseda in tvit:
        if (beseda[0] == c):
            tmp.append(izloci_besedo(beseda))
    return tmp

def omembe(tviti):
    b = dict()
    for tvit in tviti:
        if avtor(tvit) in b:
            for e in vse_afne(tvit):
                 b[avtor(tvit)].append(e)
        else:
            b[avtor(tvit)] = vse_afne(tvit)
    return b


def neomembe(ime, omembe):
    return list((set(list(omembe.keys())) - set(omembe[ime])) - set([ime]))

def se_poznata(ime, ime2, omembe):
    if ime  in omembe.keys() and ime2  in omembe.keys():
        if ime2 in omembe[ime] or ime in omembe[ime2]:
            return True
    return False

def vsi_hashtagi(tviti):
    tmp = []
    c = "#"
    for tvit in tviti:
        tvit = tvit.split(" ")
        for beseda in tvit:
            if (beseda[0] == c):
                beseda = izloci_besedo(beseda)
                if beseda not in tmp:
                    tmp.append(beseda)
    return tmp


def hashtagi(tviti):
    b = {}
    hasht = vsi_hashtagi(tviti)
    for tag in hasht:
        b[tag] = []
        for tvit in tviti:
            oseba = avtor(tvit)
            tvit = tvit.split(" ")
            for beseda in tvit:
                if beseda[0] == "#" and izloci_besedo(beseda) == tag:
                        b[tag].insert(0,oseba)
    for h in hasht:
        b[h].sort()
    return b



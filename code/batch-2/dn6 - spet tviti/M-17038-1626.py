def omembe(tviti):
    mention= {}
    for tvit in tviti:
        if avtor(tvit) in mention.keys () :
            mention[avtor(tvit)] += (se_zacne_z(tvit, "@" ))
        else:
            mention[avtor(tvit)]=(se_zacne_z(tvit , "@" ))

    return mention



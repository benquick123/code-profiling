
def avtor(tvit):
    t2 = []
    for črka in tvit:
        if črka != ":":
            t2.append(črka)

        else:
           return "".join (t2)

    return t2

def unikati (s):
    se= []
    for stvar in s:
        if stvar not in se:
            se.append (stvar)
    return se

def se_zacne_z(tvit, c):
    s=[]
    tviti=tvit.split()

    for beseda in tviti:
        if beseda.startswith(c):
            s.append( izloci_besedo(beseda))
    return s

def izloci_besedo(beseda):
    s=[]

    for letter in beseda:
        if letter.isalnum () or letter== "-":
            s.append(letter)

    def custva(tviti, hashtagi):
        s = []
        for tvit in tviti:
            avtor1 = avtor(tvit)
            hash = se_zacne_z(tvit, "#")
            for tag in hashtagi:
                if (tag in hash):
                    s.append(avtor1)

        s.sort()

        return unikati(s)

    return "".join(s)


def besedilo(tvit):
    this= []
    notime = False
    for črka in tvit:
        if notime == True  :
            this.append(črka)
        if črka== ":":
            notime = True
    for all in this:
        if all == " ":
            this.remove(all)
            break

    return "".join(this)

def zadnji_tvit(tviti):
    tweets= {}
    for tvit in tviti:
        tweets[avtor(tvit)] = besedilo(tvit)

    return tweets

def prvi_tvit(tviti):
    tweets= {}
    for tvit in tviti:
        if avtor(tvit) not in tweets:
            tweets[avtor(tvit)] = besedilo(tvit)

    return tweets

